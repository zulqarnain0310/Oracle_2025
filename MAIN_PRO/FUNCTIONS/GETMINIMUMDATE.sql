  CREATE OR REPLACE EDITIONABLE FUNCTION "MAIN_PRO"."GETMINIMUMDATE" 
(
  iv_DATE IN VARCHAR2,
  iv_DATE1 IN VARCHAR2,
  iv_DATE2 IN VARCHAR2
)
RETURN VARCHAR2
AS
   v_DATE VARCHAR2(200) := iv_DATE;
   v_DATE1 VARCHAR2(200) := iv_DATE1;
   v_DATE2 VARCHAR2(200) := iv_DATE2;
   v_temp VARCHAR2(200);

BEGIN
   v_DATE := CASE 
                  WHEN v_DATE IN ( '1900-01-01','2099-01-01' )
                   THEN NULL
   ELSE v_DATE
      END ;
   v_DATE1 := CASE 
                   WHEN v_DATE1 IN ( '1900-01-01','2099-01-01' )
                    THEN NULL
   ELSE v_DATE1
      END ;
   v_DATE2 := CASE 
                   WHEN v_DATE2 IN ( '1900-01-01','2099-01-01' )
                    THEN NULL
   ELSE v_DATE2
      END ;
   SELECT CASE 
               WHEN NVL(v_DATE, '9999-01-01') <= NVL(v_DATE1, '9999-01-01')
                 AND NVL(v_DATE, '9999-01-01') <= NVL(v_DATE2, '9999-01-01') THEN v_DATE
               WHEN NVL(v_DATE1, '9999-01-01') <= NVL(v_DATE, '9999-01-01')
                 AND NVL(v_DATE1, '9999-01-01') <= NVL(v_DATE2, '9999-01-01') THEN v_DATE1
               WHEN NVL(v_DATE2, '9999-01-01') <= NVL(v_DATE, '9999-01-01')
                 AND NVL(v_DATE2, '9999-01-01') <= NVL(v_DATE1, '9999-01-01') THEN v_DATE2
          ELSE NULL
             END col  

     INTO v_temp
     FROM DUAL ;
   RETURN v_temp;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

