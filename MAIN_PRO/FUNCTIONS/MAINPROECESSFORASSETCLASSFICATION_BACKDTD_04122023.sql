=============================================================*/
(
  iv_ProcessingDate IN VARCHAR2,
  v_UserLoginID IN VARCHAR2,
  v_Result OUT NUMBER/* DEFAULT 0*/
)
RETURN NUMBER
AS
   v_ProcessingDate VARCHAR2(200) := iv_ProcessingDate;
   v_TIMEKEY NUMBER(10,0) := 0;
   v_Month NUMBER(10,0) := 0;
   v_TwoCount NUMBER(10,0) := 0;
   v_JobStatus VARCHAR2(100) := ' ';

BEGIN

   SELECT TIMEKEY 

     INTO v_TIMEKEY
     FROM SysDataMatrix 
    WHERE  MOC_Initialised = 'Y'
             AND NVL(MOC_Frozen, 'N') = 'N';
   SELECT UTILS.CONVERT_TO_VARCHAR2(DATE_,200) 

     INTO v_ProcessingDate
     FROM SysDayMatrix 
    WHERE  TimeKey = v_TIMEKEY;
   /*------------------InsertDataforAssetClassficatio------------------*/
   DBMS_OUTPUT.PUT_LINE(1);
   IF utils.object_id('TWOParamaters', 'U') IS NOT NULL THEN

   BEGIN
      EXECUTE IMMEDIATE 'DROP TABLE TWOParamaters';
      DELETE FROM TWOParamaters;
      INSERT INTO TWOParamaters
        ( ProcessingDate, UserLoginID )
        VALUES ( UTILS.CONVERT_TO_VARCHAR2(v_ProcessingDate,200), v_UserLoginID );

   END;
   END IF;
   v_Month := utils.month_(v_ProcessingDate) ;
   SELECT COUNT(1)  

     INTO v_TwoCount
     FROM ExceptionFinalStatusType 
    WHERE  StatusType = 'TWO'
             AND utils.month_(StatusDate) = utils.month_(v_ProcessingDate)
             AND utils.year_(StatusDate) = utils.year_(v_ProcessingDate);
   DBMS_OUTPUT.PUT_LINE('@TwoCount');
   DBMS_OUTPUT.PUT_LINE(v_TwoCount);
   IF v_TwoCount = 0 THEN

   BEGIN
      DBMS_OUTPUT.PUT_LINE('SAC');
      v_Result := -5 ;
      RETURN v_Result;

   END;
   END IF;
   SELECT CASE NVL(jh.run_status, 5)
                                    WHEN 0 THEN 'Error Failed'
                                    WHEN 1 THEN 'Succeeded'
                                    WHEN 2 THEN 'Retry'
                                    WHEN 3 THEN 'Cancelled'
                                    WHEN 4 THEN 'In Progress'
                                    WHEN 5 THEN 'Other'
          ELSE 'Processing'
             END 

     INTO v_JobStatus
     FROM ( msdb.sysjobactivity ja
            LEFT JOIN msdb.sysjobhistory jh   ON ja.job_history_id = jh.instance_id
             ) 
            JOIN msdb.sysjobs_view j   ON ja.job_id = j.job_id
    WHERE  ja.session_id = ( SELECT MAX(session_id)  
                             FROM msdb.sysjobactivity  )
             AND j.NAME = 'MAINPROECESSFORASSETCLASSFICATION_BACKDTD_TWO';
   DBMS_OUTPUT.PUT_LINE(v_JobStatus);
   IF ( v_JobStatus = 'Processing'
     OR v_JobStatus = 'In Progress' ) THEN

   BEGIN
      v_Result := -2 ;
      RETURN v_Result;

   END;
   ELSE

   BEGIN
      msdb.sp_start_job('MAINPROECESSFORASSETCLASSFICATION_BACKDTD_TWO') ;
      v_Result := 1 ;
      RETURN v_Result;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

