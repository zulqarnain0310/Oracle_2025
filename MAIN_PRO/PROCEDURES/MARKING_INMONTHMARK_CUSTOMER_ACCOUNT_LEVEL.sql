DESCRIPTION : MARKING OF IN MONTH MARK AT CUSTOMER LEVEL
--EXEC [PRO].[Marking_InMonthMark_Customer_Account_level]  @timekey=25140
=============================================*/
(
  v_TIMEKEY IN NUMBER
)
AS

BEGIN
    DECLARE V_SQLERRM VARCHAR(150);
   BEGIN
      DECLARE
         v_PROCESSDATE VARCHAR2(200) ;
         v_PROC_FREQ CHAR(1) ;
         v_PROC_CONDITION NUMBER ;

      BEGIN
      
       SELECT Date_ INTO v_PROCESSDATE
           FROM RBL_MISDB_PROD.SysDayMatrix 
          WHERE  TimeKey = v_timekey ;
         SELECT RefValue INTO v_PROC_FREQ
           FROM MAIN_PRO.RefPeriod 
          WHERE  BusinessRule = 'PROC_FREQ' ;
         SELECT UTILS.CONVERT_TO_NUMBER(RefValue,18,2) INTO v_PROC_CONDITION
           FROM MAIN_PRO.RefPeriod 
          WHERE  BusinessRule = 'PROC_CONDITION' ;
      
         UPDATE GTT_CUSTOMERCAL
            SET INMONTHMARK = 'N';
         IF v_PROC_FREQ = 'D'
           AND v_PROCESSDATE <> LAST_DAY(v_PROCESSDATE) THEN

         BEGIN
            MERGE INTO GTT_CUSTOMERCAL A
            USING (SELECT A.ROWID row_id, 'Y'
            FROM GTT_CUSTOMERCAL A
                   JOIN GTT_ACCOUNTCAL B   ON A.SourceSystemCustomerID = B.SourceSystemCustomerID ) src
            ON ( A.ROWID = src.row_id )
            WHEN MATCHED THEN UPDATE SET A.INMONTHMARK = 'Y';
            UPDATE GTT_CUSTOMERCAL
               SET INMONTHMARK = 'N'
             WHERE  ( INMONTHMARK IS NULL );

         END;
         ELSE
            MERGE INTO GTT_CUSTOMERCAL A 
            USING (SELECT A.ROWID row_id, 'Y'
            FROM GTT_CUSTOMERCAL A ) src
            ON ( A.ROWID = src.row_id )
            WHEN MATCHED THEN UPDATE SET A.INMONTHMARK = 'Y';
         END IF;
         UPDATE MAIN_PRO.AclRunningProcessStatus
            SET COMPLETED = 'Y',
                ERRORDATE = NULL,
                ERRORDESCRIPTION = NULL,
                COUNT = NVL(COUNT, 0) + 1
          WHERE  RUNNINGPROCESSNAME = 'Marking_InMonthMark_Customer_Account_level';

      END;
   EXCEPTION
      WHEN OTHERS THEN

   BEGIN
      -----------------Added for DashBoard 04-03-2021
      --Update BANDAUDITSTATUS set CompletedCount=CompletedCount+1 where BandName='ASSET CLASSIFICATION'
    V_SQLERRM:=SQLERRM;      
      UPDATE MAIN_PRO.AclRunningProcessStatus
         SET COMPLETED = 'N',
             ERRORDATE = SYSDATE,
             ERRORDESCRIPTION = V_SQLERRM,
             COUNT = NVL(COUNT, 0) + 1
       WHERE  RUNNINGPROCESSNAME = 'Marking_InMonthMark_Customer_Account_level';

   END;END;

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

