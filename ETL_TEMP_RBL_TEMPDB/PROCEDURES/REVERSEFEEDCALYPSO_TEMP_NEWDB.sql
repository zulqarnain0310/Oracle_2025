   -- Insert statements for procedure here
   v_vEffectivefrom NUMBER(10,0);
   v_TimeKey NUMBER(10,0);
   v_DATE VARCHAR2(200) := ( SELECT Date_ 
     FROM RBL_MISDB_010922_UAT.Automate_Advances 
    WHERE  EXT_FLG = 'Y' );
   --GO
   /*********************************************************************************************************/
   /*  New Customers Customer Entity ID Update  */
   v_CalypsoRFEntityId NUMBER(10,0) := 0;
-- Add the parameters for the stored procedure here

BEGIN

   SELECT TimeKey 

     INTO v_vEffectiveFrom
     FROM RBL_MISDB_010922_UAT.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   SELECT TimeKey 

     INTO v_TimeKey
     FROM RBL_MISDB_010922_UAT.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   EXECUTE IMMEDIATE ' TRUNCATE TABLE TempreversefeedCalypso ';
   INSERT INTO TempreversefeedCalypso
     ( Date_of_data, source_system_name, branch_code, ucic_id, issuerId, issuuerName, Ref_Txn_Sys_Cust_ID, PAN, ISSUER_CATEGORY_CODE, GrpEntityOfBank, CP_HOLIDAY, CP_EXTERNAL_REF, CP_FINANCIAL, CP_PARENT, CP_COMMENT, CP_STATUS, COUNTERPARTY_SHORTNAME, COUNTERPARTY_FULLNAME, COUNTERPARTY_ROLE1, COUNTERPARTY_COUNTRY, COUNTERPARTY_ROLE2, COUNTERPARTY_ROLE3, COUNTERPARTY_ROLE4, COUNTERPARTY_ROLE5, CIF_ID, ENPA_ASSET_CODE, ENPA_DESCRIPTION, ENPA_ASSET_CODE_DATE, ENPA_D2K_NPA_DATE, AuthorisationStatus, EffectiveFromTimeKey, EffectiveToTimeKey, CreatedBy, DateCreated, ModifiedBy, DateModified, ApprovedBy, DateApproved )
     ( SELECT Date_of_data ,
              source_system_name ,
              branch_code ,
              ucic_id ,
              issuerId ,
              issuuerName ,
              Ref_Txn_Sys_Cust_ID ,
              PAN ,
              ISSUER_CATEGORY_CODE ,
              GrpEntityOfBank ,
              CP_HOLIDAY ,
              CP_EXTERNAL_REF ,
              CP_FINANCIAL ,
              CP_PARENT ,
              CP_COMMENT ,
              CP_STATUS ,
              COUNTERPARTY_SHORTNAME ,
              COUNTERPARTY_FULLNAME ,
              COUNTERPARTY_ROLE1 ,
              COUNTERPARTY_COUNTRY ,
              COUNTERPARTY_ROLE2 ,
              COUNTERPARTY_ROLE3 ,
              COUNTERPARTY_ROLE4 ,
              COUNTERPARTY_ROLE5 ,
              CIF_ID ,
              ENPA_ASSET_CODE ,
              ENPA_DESCRIPTION ,
              ENPA_ASSET_CODE_DATE ,
              ENPA_D2K_NPA_DATE ,
              NULL AuthorisationStatus  ,
              v_vEffectivefrom EffectiveFromTimeKey  ,
              49999 EffectiveToTimeKey  ,
              'SSISUSER' CreatedBy  ,
              SYSDATE DateCreated  ,
              NULL ModifiedBy  ,
              NULL DateModified  ,
              NULL ApprovedBy  ,
              NULL DateApproved  
       FROM RBL_STGDB.reversefeed_calypso_STG A );
   /*********************************************************************************************************/
   /*  Existing Customers Customer Entity ID Update  */
   MERGE INTO TEMP 
   USING (SELECT TEMP.ROWID row_id, MAIN.CalypsoRFEntityId
   FROM TEMP ,RBL_TEMPDB.TempReversefeedCalypso TEMP
          JOIN RBL_MISDB_010922_UAT.ReverseFeedCalypso MAIN   ON TEMP.issuerId = MAIN.issuerId 
    WHERE MAIN.EffectiveToTimeKey = 49999) src
   ON ( TEMP.ROWID = src.row_id )
   WHEN MATCHED THEN UPDATE SET TEMP.CalypsoRFEntityId = src.CalypsoRFEntityId;
   SELECT MAX(CalypsoRFEntityId)  

     INTO v_CalypsoRFEntityId
     FROM RBL_MISDB_010922_UAT.ReverseFeedCalypso ;
   IF v_CalypsoRFEntityId IS NULL THEN

   BEGIN
      v_CalypsoRFEntityId := 0 ;

   END;
   END IF;
   MERGE INTO TEMP 
   USING (SELECT TEMP.ROWID row_id, ACCT.CalypsoRFEntityId
   FROM TEMP ,RBL_TEMPDB.TempReversefeedCalypso TEMP
          JOIN ( SELECT "TEMPREVERSEFEEDCALYPSO".issuerId ,
                        (v_CalypsoRFEntityId + ROW_NUMBER() OVER ( ORDER BY ( SELECT 1 
                                                                                FROM DUAL  )  )) CalypsoRFEntityId  
                 FROM RBL_TEMPDB.TempReversefeedCalypso 
                  WHERE  "TEMPREVERSEFEEDCALYPSO".CalypsoRFEntityId = 0
                           OR "TEMPREVERSEFEEDCALYPSO".CalypsoRFEntityId IS NULL ) ACCT   ON TEMP.issuerId = ACCT.issuerId ) src
   ON ( TEMP.ROWID = src.row_id )
   WHEN MATCHED THEN UPDATE SET TEMP.CalypsoRFEntityId = src.CalypsoRFEntityId;--------------------------------------------------

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

