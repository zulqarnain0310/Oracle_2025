 -- Description:	<Description,,>
 -- =============================================

AS
   v_vEffectivefrom NUMBER(10,0);
   v_TimeKey NUMBER(10,0);
   v_DATE VARCHAR2(200) ;
-- Add the parameters for the stored procedure here

BEGIN


   SELECT Date_ INTO v_DATE 
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y' ;
   EXECUTE IMMEDIATE ' TRUNCATE TABLE TempAcDailyTxnDetail ';
   
   SELECT TimeKey 

     INTO v_vEffectiveFrom
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   SELECT TimeKey 

     INTO v_TimeKey
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   INSERT INTO RBL_TEMPDB.TempAcDailyTxnDetail
     ( Branchcode, CustomerID, CustomerAcID, AccountEntityId, TxnDate, TxnType, TxnSubType, TxnTime, CurrencyAlt_Key, CurrencyConvRate, TxnAmount, TxnAmountInCurrency, ExtDate, TxnRefNo, TxnValueDate, MnemonicCode, Particular, AuthorisationStatus, CreatedBy, DateCreated, ModifiedBy, DateModified, ApprovedBy, DateApproved, Remark, TrueCredit, IsProcessed, Balance )
     ( 
       ----FINACLE DATA------
       SELECT NULL Branchcode  ,
              ACBD.REFCUSTOMERID CustomerID  ,
              TXN.AccountID CustomerAcID  ,
              ACBD.AccountEntityId AccountEntityId  ,
              TXN.TxnDate TxnDate  ,
              CASE 
                   WHEN TXN.TxnType = 'D' THEN 'DEBIT'
                   WHEN TXN.TxnType = 'C' THEN 'CREDIT'   END TxnType  ,
              CASE 
                   WHEN TXN.TxnType = 'D'
                     AND TXN.TxnSubType = 'IC' THEN 'INTEREST'
                   WHEN TXN.TxnType = 'D' THEN 'OTHER DEBIT'
              ELSE TXN.TxnSubType
                 END TxnSubType  ,
              NULL TxnTime  ,
              DC.CurrencyAlt_Key CurrencyAlt_Key  ,
              DCC.ConvRate CurrencyConvRate  ,
              TXN.TxnAmountINR TxnAmount  ,
              TXN.TxnAmountinCurrency TxnAmountInCurrency  ,
              v_DATE ExtDate  ,
              TxnID TxnRefNo  ,
              NULL TxnValueDate  ,
              NULL MnemonicCode  ,
              TXN.TxnParticulars Particular  ,
              NULL AuthorisationStatus  ,
              'SSISUSER' CreatedBy  ,
              SYSDATE DateCreated  ,
              NULL ModifiedBy  ,
              NULL DateModified  ,
              NULL ApprovedBy  ,
              NULL DateApproved  ,
              NULL Remark  ,
              NULL TrueCredit  ,
              NULL IsProcessed  ,
              NULL Balance  
       FROM RBL_STGDB.TXN_SOURCESYSTEM01_STG txn
              LEFT JOIN RBL_TEMPDB.TempAdvAcBasicDetail ACBD   ON ACBD.CustomerAcID = TXN.AccountID
              LEFT JOIN RBL_MISDB_PROD.DimCurrency DC   ON LTRIM(RTRIM(NVL(DC.CurrencyCode, ' '))) = TXN.TxnCurrency
              AND DC.EffectiveFromTimeKey <= v_TimeKey
              AND DC.EffectiveToTimeKey >= v_TimeKey
              LEFT JOIN RBL_MISDB_PROD.DimCurCovRate DCC   ON DCC.CurrencyCode = DC.CurrencyCode
              AND DCC.ConvDate = v_DATE
              AND DCC.EffectiveFromTimeKey <= v_TimeKey
              AND DCC.EffectiveToTimeKey >= v_TimeKey );

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

