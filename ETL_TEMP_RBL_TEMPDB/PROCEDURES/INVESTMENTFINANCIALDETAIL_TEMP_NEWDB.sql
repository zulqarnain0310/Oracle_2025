     ( SELECT v_TimeKey Time_Key  ,
              NULL Inv_Key  ,
              HoldingNature ,
              C.CurrencyAlt_Key ,
              CurrencyConvRate ,
              BookType ,
              BookValue ,
              BookValueINR ,
              MTMValue ,
              MTMValueINR ,
