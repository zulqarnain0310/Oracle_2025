              49999 EffectiveToTimeKey  ,
              'SSISUSER' CreatedBy  ,
              SYSDATE DateCreated  ,
              NULL ModifiedBy  ,
              NULL DateModified  ,
              NULL ApprovedBy  ,
              NULL DateApproved  ,
              BalanceSheetDate ,
              ListedShares 
       FROM RBL_STGDB.INVFINANCIAL_SOURCESYSTEM_STG A
              JOIN RBL_MISDB_010922_UAT.DimAssetClass B   ON A.AssetClass = B.SrcSysClassCode
              AND B.EffectiveFromTimeKey <= v_TimeKey
              AND B.EffectiveToTimeKey >= v_TimeKey
              JOIN RBL_MISDB_010922_UAT.DimCurrency C   ON A.CurrencyCode = C.SrcSysCurrencyCode
              AND C.EffectiveFromTimeKey <= v_TimeKey
              AND C.EffectiveToTimeKey >= v_TimeKey );

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

