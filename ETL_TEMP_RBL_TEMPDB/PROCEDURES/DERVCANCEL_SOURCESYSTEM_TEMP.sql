  CREATE OR REPLACE EDITIONABLE PROCEDURE "ETL_TEMP_RBL_TEMPDB"."DERVCANCEL_SOURCESYSTEM_TEMP" 
AS
   v_vEffectivefrom NUMBER(10,0);
   v_TimeKey NUMBER(10,0);
   v_DATE VARCHAR2(200) ;
   --GO
   /*********************************************************************************************************/
   /*  New Customers Customer Entity ID Update  */
   v_DerivativeEntityId NUMBER(10,0) := 0;

BEGIN

   SELECT Date_ INTO v_DATE 
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y' ;

   SELECT TimeKey 

     INTO v_vEffectiveFrom
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   SELECT TimeKey 

     INTO v_TimeKey
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   EXECUTE IMMEDIATE ' TRUNCATE TABLE RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  ';
   INSERT INTO RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM 
     ( DateofData, SourceSystem, BranchCode, UCIC_ID, CustID, CustomerName, AcID, DerivativeRefNo, Duedate, DueAmt, Os_Amt, POS, AuthorisationStatus, EffectiveFromTimeKey, EffectiveToTimeKey, CreatedBy, DateCreated, ModifiedBy, DateModified, ApprovedBy, DateApproved )
     ( SELECT A.DateofData ,
              SourceSystem ,
              BranchCode ,
              UCIC_ID ,
              CustID ,
              CustomerName ,
              AcID ,
              DerivativeRefNo ,
              Duedate ,
              DueAmt ,
              Os_Amt ,
              POS ,
              NULL AuthorisationStatus  ,
              v_vEffectivefrom EffectiveFromTimeKey  ,
              49999 EffectiveToTimeKey  ,
              'SSISUSER' CreatedBy  ,
              SYSDATE DateCreated  ,
              NULL ModifiedBy  ,
              NULL DateModified  ,
              NULL ApprovedBy  ,
              NULL DateApproved  
       FROM RBL_STGDB.DERVCANCEL_SOURCESYSTEM_STG A );
   /*********************************************************************************************************/
   /*  Existing Customers Customer Entity ID Update  */
   MERGE INTO RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  TEMP
   USING (SELECT TEMP.ROWID row_id, MAIN.DerivativeEntityId
   FROM RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  TEMP
          JOIN CurDat_RBL_MISDB_PROD.DerivativeDetail MAIN   ON TEMP.DerivativeRefNo = MAIN.DerivativeRefNo 
    WHERE MAIN.EffectiveToTimeKey = 49999) src
   ON ( TEMP.ROWID = src.row_id )
   WHEN MATCHED THEN UPDATE SET TEMP.DerivativeEntityID = src.DerivativeEntityId;
   SELECT MAX(DerivativeEntityId)  

     INTO v_DerivativeEntityId
     FROM CurDat_RBL_MISDB_PROD.DerivativeDetail ;
   IF v_DerivativeEntityId IS NULL THEN

   BEGIN
      v_DerivativeEntityId := 0 ;

   END;
   END IF;
   MERGE INTO RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  TEMP
   USING (SELECT TEMP.ROWID row_id, ACCT.DerivativeEntityId
   FROM RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  TEMP
          JOIN ( SELECT A.DerivativeRefNo ,
                        (v_DerivativeEntityId + ROW_NUMBER() OVER ( ORDER BY ( SELECT 1 
                                                                                 FROM DUAL  )  )) DerivativeEntityId  
                 FROM RBL_TEMPDB.TEMPDERVCANCEL_SOURCESYSTEM  A
                  WHERE  A.DerivativeEntityID = 0
                           OR A.DerivativeEntityID IS NULL ) ACCT   ON TEMP.DerivativeRefNo = ACCT.DerivativeRefNo ) src
   ON ( TEMP.ROWID = src.row_id )
   WHEN MATCHED THEN UPDATE SET TEMP.DerivativeEntityID = src.DerivativeEntityId;--------------------------------------------------

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

