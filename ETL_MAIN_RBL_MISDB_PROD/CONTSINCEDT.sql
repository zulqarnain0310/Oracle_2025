  CREATE OR REPLACE EDITIONABLE PROCEDURE "ETL_MAIN_RBL_MISDB_PROD"."CONTSINCEDT" 
AS
   v_Timekey NUMBER(10,0);

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM RBL_MISDB_PROD.Automate_Advances 
    WHERE  EXT_FLG = 'Y';
   UPDATE MAIN_PRO.ContExcsSinceDtAccountCal_RBL
      SET EffectiveToTimeKey = v_Timekey - 1
    WHERE  EffectiveFromTimeKey < v_Timekey
     AND EffectiveToTimeKey = 49999;
   DELETE MAIN_PRO.ContExcsSinceDtAccountCal_RBL

    WHERE  EffectiveFromTimeKey = v_Timekey;
   INSERT INTO MAIN_PRO.ContExcsSinceDtAccountCal_RBL
     ( CustomerAcID, AccountEntityId, SanctionAmt, SanctionDt, Balance, DrawingPower, ContExcsSinceDt, EffectiveFromTimeKey, EffectiveToTimeKey )
     ( SELECT A.CustomerAcID ,
              A.AccountEntityId ,
              currentLimit SanctionAmt  ,
              CurrentLimitDt SanctionDt  ,
              c.Balance ,
              DrawingPower ,
              ContExcsSinceDt ,
              v_Timekey ,
              49999 
       FROM RBL_TEMPDB.TempAdvAcBasicDetail A
              JOIN RBL_TEMPDB.TempAdvFacCCDetail B   ON A.AccountEntityId = B.AccountEntityId
              JOIN RBL_TEMPDB.TempAdVAcBalanceDetail C   ON C.AccountEntityId = A.AccountEntityId
              JOIN RBL_TEMPDB.TempAdVAcBalanceDetail D   ON D.AccountEntityId = A.AccountEntityId
              JOIN RBL_TEMPDB.TempAdvAcFinancialDetail E   ON E.AccountEntityId = A.AccountEntityId
        WHERE  ContExcsSinceDt IS NOT NULL );
   UPDATE MAIN_PRO.ContExcsSinceDtDebitAccountCal
      SET EffectiveToTimeKey = v_Timekey - 1
    WHERE  EffectiveFromTimeKey < v_Timekey
     AND EffectiveToTimeKey = 49999;
   DELETE MAIN_PRO.ContExcsSinceDtDebitAccountCal

    WHERE  EffectiveFromTimeKey = v_Timekey;
   -----
   INSERT INTO MAIN_PRO.ContExcsSinceDtDebitAccountCal
     ( CustomerAcID, AccountEntityId, SanctionAmt, SanctionDt, Balance, DrawingPower, ContExcsSinceDebitDt, EffectiveFromTimeKey, EffectiveToTimeKey )
     ( SELECT A.CustomerAcID ,
              A.AccountEntityId ,
              A.currentLimit SanctionAmt  ,
              CurrentLimitDt SanctionDt  ,
              c.Balance ,
              E.DrawingPower ,
              DebitSinceDate ,
              v_Timekey ,
              49999 
       FROM RBL_TEMPDB.TempAdvAcBasicDetail A
              JOIN RBL_TEMPDB.TempAdvFacCCDetail B   ON A.AccountEntityId = B.AccountEntityId
              JOIN RBL_TEMPDB.TempAdVAcBalanceDetail C   ON C.AccountEntityId = A.AccountEntityId
              JOIN RBL_TEMPDB.TempAdvAcFinancialDetail E   ON E.AccountEntityId = A.AccountEntityId
              JOIN RBL_STGDB.ACCOUNT_ALL_SOURCE_SYSTEM AA   ON AA.CustomerAcID = A.CustomerACID
        WHERE  AA.DebitSinceDate IS NOT NULL );

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

