  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."REPORTHISTORY" 
(
  v_UserLoginID IN VARCHAR2 DEFAULT ' ' ,
  v_fromdate IN VARCHAR2 DEFAULT ' ' ,
  v_todate IN VARCHAR2 DEFAULT ' ' ,
  v_status IN VARCHAR2 DEFAULT ' ' ,
  v_Result OUT NUMBER/* DEFAULT 0*/
)
RETURN NUMBER
AS
   v_cursor SYS_REFCURSOR;
--SELECT * FROM RDMHistoryDetail    

BEGIN

   BEGIN
      DECLARE
         -- BEGIN TRANSACTION    
         v_RDMHistEntityId NUMBER(10,0) := ( SELECT NVL(MAX(RDMHistEntityId) , 0) + 1 
           FROM RDMHistoryDetail  );

      BEGIN
         /*TODO:SQLDEV*/ SET DATEFORMAT DMY /*END:SQLDEV*/
         INSERT INTO RDMHistoryDetail
           ( RDMHistEntityId, Fromdate, Todate, RDMStatus, CreatedBy, DateCreated )
           ( SELECT v_RDMHistEntityId ,
                    UTILS.CONVERT_TO_VARCHAR2(v_fromdate,200,p_style=>103) FromDate  ,
                    UTILS.CONVERT_TO_VARCHAR2(v_todate,200,p_style=>103) ToDate  ,
                    v_status RDMStatus  ,
                    v_UserLoginID CreatedBy  ,
                    SYSDATE DateCreated  
               FROM DUAL  );
         -- COMMIT TRANSACTION    
         v_Result := 1 ;
         RETURN v_Result;

      END;
   EXCEPTION
      WHEN OTHERS THEN

   BEGIN
      ROLLBACK;
      utils.resetTrancount;
      INSERT INTO RBL_MISDB_PROD.Error_Log
        ( SELECT utils.error_line ErrorLine  ,
                 SQLERRM ErrorMessage  ,
                 SQLCODE ErrorNumber  ,
                 utils.error_procedure ErrorProcedure  ,
                 utils.error_severity ErrorSeverity  ,
                 utils.error_state ErrorState  ,
                 SYSDATE 
            FROM DUAL  );
      OPEN  v_cursor FOR
         SELECT SQLERRM 
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      RETURN -1;

   END;END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

