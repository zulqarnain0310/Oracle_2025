  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."BUSINESSDAY" 
--DROP FUNCTION BusinessDay

(
  iv_date IN VARCHAR2
)
RETURN VARCHAR2
AS
   v_date VARCHAR2(200) := iv_date;
   v_temp NUMBER(1, 0) := 0;

BEGIN
   LOOP
      BEGIN
         SELECT 1 INTO v_temp
           FROM DUAL
          WHERE ( SELECT utils.datename('WEEKDAY', v_date) 
                    FROM DUAL  ) = 'Sunday';
      EXCEPTION
         WHEN OTHERS THEN
            NULL;
      END;

      IF v_temp != 1 THEN
         EXIT;
      END IF;


      BEGIN
         v_date := (utils.dateadd('DD', -1, v_date)) ;

      END;
   END LOOP;
   RETURN v_date;--GO 
   --SELECT BusinessDay('2018-06-17')

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

