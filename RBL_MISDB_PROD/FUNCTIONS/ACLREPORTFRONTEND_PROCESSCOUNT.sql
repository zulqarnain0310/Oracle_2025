  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."ACLREPORTFRONTEND_PROCESSCOUNT" 
(
  v_UserLoginID IN VARCHAR2,
  v_Result OUT NUMBER
)
RETURN NUMBER
AS
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;
--DECLARE
--  @UserLoginID Varchar(100)='C36601'
--  --@Result int --OUTPUT

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE 0 < ( SELECT COUNT(*)  
                   FROM DPDCSVUpload  );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT * 
           FROM DPDCSVUpload  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);--where UserLoginId = @UserLoginID
      EXECUTE IMMEDIATE ' TRUNCATE TABLE DPDCSVUpload ';
      OPEN  v_cursor FOR
         SELECT CASE 
                     WHEN NVL(COUNT, 0) = 0 THEN 0
                ELSE COUNT
                   END COUNT  ,
                FILENAME ,
                UserLoginId ,
                'SuccessCountDetails' TableName  
           FROM IRAC_Success_ACL_UploadCount 
          WHERE  UserLoginId = v_UserLoginID ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      DBMS_OUTPUT.PUT_LINE('Result=1');
      v_Result := 1 ;
      RETURN v_Result;

   END;
   ELSE

   BEGIN
      DBMS_OUTPUT.PUT_LINE('Result=0');
      v_Result := 0 ;
      RETURN v_Result;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

