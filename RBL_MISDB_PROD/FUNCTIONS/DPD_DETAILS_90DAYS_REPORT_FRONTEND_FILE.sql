  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."DPD_DETAILS_90DAYS_REPORT_FRONTEND_FILE" 
(
  v_UserLoginID IN VARCHAR2,
  v_Result OUT NUMBER
)
RETURN NUMBER
AS
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE ( SELECT COUNT(*)  
               FROM DPD_90DAYS_HistoryReport 
                WHERE  UserLoginId = v_UserLoginID ) > 0;
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT Timekey ,
                UTILS.CONVERT_TO_VARCHAR2(Process_Date,10,p_style=>105) Process_Date  ,
                UCIF_ID ,
                CustomerID ,
                CustomerAcID ,
                DPD_IntNotServicedDt ,
                DPD_IntOverdueSince ,
                DPD_Overdrawn ,
                DPD_Overdue ,
                DPD_Renewal ,
                DPD_StockStmt ,
                DPD_NoCredit ,
                DPD_PrincOverdue ,
                DPD_OtherOverdueSince ,
                MAX_DPD ,
                UserLoginID 
           FROM DPD_90DAYS_HistoryReport 
          WHERE  UserLoginId = v_UserLoginID
           ORDER BY UCIF_ID,
                    CustomerID,
                    CustomerAcID ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      v_Result := 1 ;
      RETURN v_result;

   END;
   ELSE

   BEGIN
      v_Result := 0 ;
      RETURN v_result;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

