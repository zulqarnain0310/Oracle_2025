  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."D2K_SPLIT" 
(
  iv_String IN NVARCHAR2,
  v_Delimiter IN CHAR
)
RETURN RBL_MISDB_PROD.D2K_Split_pkg.tt_v_Results_type PIPELINED
AS
   v_String NVARCHAR2(2000) := iv_String;
   v_INDEX NUMBER(10,0);
   v_SLICE NVARCHAR2(2000);
   v_temp SYS_REFCURSOR;
   v_temp_1 TT_V_RESULTS%ROWTYPE;
   v_temp_2 SYS_REFCURSOR;
   v_temp_3 TT_V_RESULTS%ROWTYPE;

BEGIN
   -- HAVE TO SET TO 1 SO IT DOESNT EQUAL Z
   --     ERO FIRST TIME IN LOOP
   v_INDEX := 1 ;
   -- following line added 10/06/04 as null
   --      values cause issues
   IF v_String IS NULL THEN
    OPEN v_tempv_temp_2 FOR
      SELECT * 
        FROM tt_v_Results;

   LOOP
      FETCH v_tempv_temp_2 INTO v_temp_1v_temp_3;
      EXIT WHEN v_tempv_temp_2%NOTFOUND;
      PIPE ROW ( v_temp_1v_temp_3 );
   END LOOP;
   END IF;
   WHILE v_INDEX != 0 
   LOOP 

      BEGIN
         -- GET THE INDEX OF THE FIRST OCCURENCE OF THE SPLIT CHARACTER
         v_INDEX := INSTR(RTRIM(LTRIM(v_STRING)), v_Delimiter) ;
         -- NOW PUSH EVERYTHING TO THE LEFT OF IT INTO THE SLICE VARIABLE
         IF v_INDEX != 0 THEN
          v_SLICE := SUBSTR(RTRIM(LTRIM(v_STRING)), 0, v_INDEX - 1) ;
         ELSE
            v_SLICE := RTRIM(LTRIM(v_STRING)) ;
         END IF;
         -- PUT THE ITEM INTO THE RESULTS SET
         INSERT INTO tt_v_Results
           ( Items )
           VALUES ( v_SLICE );
         -- CHOP THE ITEM REMOVED OFF THE MAIN STRING
         v_STRING := SUBSTR(v_STRING, -LENGTH(RTRIM(LTRIM(v_STRING))) - v_INDEX, LENGTH(RTRIM(LTRIM(v_STRING))) - v_INDEX) ;
         -- BREAK OUT IF WE ARE DONE
         IF LENGTH(v_STRING) = 0 THEN
          EXIT;
         END IF;

      END;
   END LOOP;
   OPEN v_tempv_temp_2 FOR
      SELECT * 
        FROM tt_v_Results;

   LOOP
      FETCH v_tempv_temp_2 INTO 
      EXIT WHEN v_tempv_temp_2%NOTFOUND;
      PIPE ROW (  );
   END LOOP;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

