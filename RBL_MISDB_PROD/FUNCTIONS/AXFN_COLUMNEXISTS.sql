  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."AXFN_COLUMNEXISTS" 
(
  v_TableName IN VARCHAR2,
  v_ColumnName IN VARCHAR2
)
RETURN VARCHAR2
AS
   v_Result VARCHAR2(100);
   v_temp NUMBER(1, 0) := 0;

BEGIN
   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE EXISTS ( SELECT 1 
                      FROM INFORMATION_SCHEMA.COLUMNS_ 
                       WHERE  TABLE_NAME = v_TableName
                                AND COLUMN_NAME = v_ColumnName );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      v_Result := 'Already Exsits' ;

   END;
   ELSE

   BEGIN
      v_Result := 'Not Available, You can create now!' ;

   END;
   END IF;
   RETURN (v_Result);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

