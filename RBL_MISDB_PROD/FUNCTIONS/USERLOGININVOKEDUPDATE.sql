  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."USERLOGININVOKEDUPDATE" 
(
  v_UserLoginID IN VARCHAR2,
  v_LoginPassword IN VARCHAR2,
  v_TimeKey IN NUMBER,-- NITIN : 21 DEC 2010
  v_Result OUT NUMBER/* DEFAULT 0*/
)
RETURN NUMBER
AS
   v_temp NUMBER(1, 0) := 0;

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE NOT EXISTS ( SELECT * 
                          FROM DimUserInfo 
                           WHERE  ( DimUserInfo.EffectiveFromTimeKey <= v_TimeKey
                                    AND DimUserInfo.EffectiveToTimeKey >= v_TimeKey )
                                    AND UserLoginID = v_UserLoginID
                                    AND UserLogged = 1 );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      --SQL Server BEGIN TRANSACTION;
      utils.incrementTrancount;
      ROLLBACK;
      utils.resetTrancount;
      v_Result := -1 ;
      RETURN -1;

   END;
   ELSE
   DECLARE
      v_LocationCode VARCHAR2(50);
      v_Location VARCHAR2(10);
      v_Count NUMBER(10,0);

   BEGIN
      UPDATE DimUserInfo
         SET UserLogged = 0
       WHERE  UserLoginID = v_UserLoginID
        AND ( EffectiveFromTimeKey <= v_TimeKey
        AND EffectiveToTimeKey >= v_TimeKey );
      SELECT UserLocationCode ,
             UserLocation 

        INTO v_LocationCode,
             v_Location
        FROM DimUserInfo 
       WHERE  UserLoginID = v_UserLoginID
                AND ( EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey );
      DBMS_OUTPUT.PUT_LINE(v_Location);
      IF ( v_Location = 'HO' ) THEN

      BEGIN
         SELECT UserLoginCount 

           INTO v_Count
           FROM DimMaxLoginAllow 
          WHERE  UserLocation = v_Location;

      END;
      END IF;
      IF ( v_Location = 'RO' ) THEN

      BEGIN
         SELECT UserLoginCount 

           INTO v_Count
           FROM DimMaxLoginAllow 
          WHERE  UserLocation = v_Location
                   AND UserLocationCode = v_LocationCode;

      END;
      END IF;
      IF ( v_Location = 'BO' ) THEN

      BEGIN
         SELECT UserLoginCount 

           INTO v_Count
           FROM DimMaxLoginAllow 
          WHERE  UserLocation = v_Location
                   AND UserLocationCode = v_LocationCode;

      END;
      END IF;
      IF ( v_Count = 0 ) THEN

      BEGIN
         v_Count := v_Count + 1 ;

      END;
      ELSE

      BEGIN
         v_Count := v_Count - 1 ;

      END;
      END IF;
      --  EXEC sp_UpdateUserAccessCount @LocationCode,@Count,@Location
      v_Result := 1 ;
      RETURN 1;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

