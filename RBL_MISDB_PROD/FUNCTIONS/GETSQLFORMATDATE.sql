  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."GETSQLFORMATDATE" 
(
  iv_asOnDate IN VARCHAR2
)
RETURN DATE
AS
   v_asOnDate VARCHAR2(55) := iv_asOnDate;
   v_Month VARCHAR2(5);
   v_day VARCHAR2(5);
   v_Year VARCHAR2(5);
   v_loc1 NUMBER(10,0);
   v_loc2 NUMBER(10,0);

BEGIN
   v_asOnDate := REPLACE(v_asOnDate, ' ', ' ') ;
   v_asOnDate := REPLACE(v_asOnDate, '-', '/') ;
   v_asOnDate := REPLACE(v_asOnDate, '.', '/') ;
   v_loc1 := INSTR(v_asOnDate, '/', 0) ;
   v_loc2 := INSTR(v_asOnDate, '/', v_loc1 + 1) ;
   v_day := SUBSTR(v_asOnDate, 0, v_loc1) ;
   v_Month := SUBSTR(v_asOnDate, v_loc1 + 1, v_loc2 - (v_loc1 + 1)) ;
   v_Year := SUBSTR(v_asOnDate, v_loc2 + 1, LENGTH(v_asOnDate)) ;
   IF (LENGTH(v_Year)) = 2 THEN

   BEGIN
      v_Year := v_Year + 2000 ;

   END;
   END IF;
   v_asOnDate := v_Year || '-' || v_Month || '-' || v_day ;
   RETURN v_asOnDate;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

