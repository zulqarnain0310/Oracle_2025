  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."ACCELERATEDPROVISION_JOB_04122023" 
(
  v_ProcessingDate IN VARCHAR2,
  v_UserLoginID IN VARCHAR2,
  v_Result OUT NUMBER/* DEFAULT 1*/
)
RETURN NUMBER
AS
   v_JobStatus VARCHAR2(100) := ' ';
   v_TIMEKEY NUMBER(10,0) := 0;
   --Declare @Month INT=0
   v_TwoCount NUMBER(10,0) := 0;

BEGIN

   INSERT INTO RBL_MISDB_PROD.AcceleratedProVisionProcessinDate
     ( ProcessingDate )
     VALUES ( v_ProcessingDate );
   SELECT TIMEKEY 

     INTO v_TIMEKEY
     FROM SysDayMatrix 
    WHERE  UTILS.CONVERT_TO_VARCHAR2(DATE_,200) = UTILS.CONVERT_TO_VARCHAR2(v_ProcessingDate,200);
   SELECT CASE NVL(jh.run_status, 5)
                                    WHEN 0 THEN 'Error Failed'
                                    WHEN 1 THEN 'Succeeded'
                                    WHEN 2 THEN 'Retry'
                                    WHEN 3 THEN 'Cancelled'
                                    WHEN 4 THEN 'In Progress'
                                    WHEN 5 THEN 'Other'
          ELSE 'Processing'
             END 

     INTO v_JobStatus
     FROM ( msdb.sysjobactivity ja
            LEFT JOIN msdb.sysjobhistory jh   ON ja.job_history_id = jh.instance_id
             ) 
            JOIN msdb.sysjobs_view j   ON ja.job_id = j.job_id
    WHERE  ja.session_id = ( SELECT MAX(session_id)  
                             FROM msdb.sysjobactivity  )
             AND j.NAME = 'AcceleratedProvisionProcess_BackDtd';
   DBMS_OUTPUT.PUT_LINE(v_JobStatus);
   IF ( v_JobStatus = 'Processing'
     OR v_JobStatus = 'In Progress' ) THEN

   BEGIN
      v_Result := -2 ;
      RETURN v_Result;

   END;
   ELSE

   BEGIN
      msdb.sp_start_job('AcceleratedProvisionProcess_BackDtd') ;
      v_Result := 1 ;
      RETURN v_Result;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

