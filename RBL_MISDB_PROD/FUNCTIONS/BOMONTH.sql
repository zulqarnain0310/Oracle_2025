  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."BOMONTH" 
-- =============================================
 -- Author:        DEEPAK JANGRA
 -- Create date: 20th FEB 2018
 -- Description:    Function returns first day of month [date]
 -- =============================================

(
  v_dateIN IN VARCHAR2
)
RETURN VARCHAR2
AS
   v_Result VARCHAR2(200);

BEGIN
   v_Result := utils.dateadd('DD', -utils.day_(v_dateIN) + 1, v_dateIN) ;
   RETURN v_Result;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

