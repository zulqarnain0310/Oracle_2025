  CREATE OR REPLACE EDITIONABLE FUNCTION "RBL_MISDB_PROD"."DATECONVERT" 
(
  v_String IN VARCHAR2
)
RETURN RBL_MISDB_PROD.DateConvert_pkg.tt_v_Results_2_type PIPELINED
AS
   v_Date VARCHAR2(20) := v_String;
   v_MM VARCHAR2(2);
   v_DD VARCHAR2(2);
   v_YY VARCHAR2(4);
   v_RDate VARCHAR2(200);
   v_temp SYS_REFCURSOR;
   v_temp_1 TT_V_RESULTS_2%ROWTYPE;

BEGIN
   v_DD := SUBSTR(v_Date, 0, 2) ;
   v_MM := SUBSTR(SUBSTR(v_Date, 0, 5), -2, 2) ;
   v_YY := SUBSTR(v_Date, -4, 4) ;
   v_RDate := UTILS.CONVERT_TO_VARCHAR2((v_YY || '-' || v_MM || '-' || v_DD),200) ;
   INSERT INTO tt_v_Results_2
     ( RDATE )
     VALUES ( v_RDate );
   OPEN v_temp FOR
      SELECT * 
        FROM tt_v_Results_2;

   LOOP
      FETCH v_temp INTO v_temp_1;
      EXIT WHEN v_temp%NOTFOUND;
      PIPE ROW ( v_temp_1 );
   END LOOP;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

