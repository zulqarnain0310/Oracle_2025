      OPEN  v_cursor FOR
         SELECT AccountID Investment_ID_Derivative_Ref_No  ,
                --'Details' as TableName,
                AdditionalProvisionAbsolute Additional_Provision_Absolute_in_Rs_  ,
                UTILS.CONVERT_TO_VARCHAR2(A.FraudDate,10,p_style=>103) Fraud_Date  ,
