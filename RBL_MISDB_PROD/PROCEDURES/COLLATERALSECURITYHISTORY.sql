  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."COLLATERALSECURITYHISTORY" 
(
  v_Code IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.SecurityMappingAlt_Key ,
             B.SourceName ,
             B.SourceAlt_Key ,
             A.SrcSysSecurityCode ,
             A.SrcSysSecurityName ,
             A.SecurityName ,
             A.SecurityAlt_Key ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  
        FROM DimCollateralSecurityMapping A
               JOIN DIMSOURCEDB B   ON A.SourceAlt_Key = B.SourceAlt_Key
       WHERE  A.SrcSysSecurityCode = v_Code ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

