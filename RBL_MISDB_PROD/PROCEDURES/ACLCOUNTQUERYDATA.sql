set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."ACLCOUNTQUERYDATA" 
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT UTILS.CONVERT_TO_VARCHAR2(Process_date,200,p_style=>105) Date_  ,
             SourceName ,
             AssetClassName ,
             COUNT(DISTINCT CustomerAcid)  COUNT  
        FROM ACL_NPA_DATA A
               JOIN DimAssetClassMapping B   ON A.FinalAssetClassAlt_Key = B.AssetClassAlt_Key
       WHERE  UTILS.CONVERT_TO_VARCHAR2(Process_date,200,p_style=>105) IN ( '10/26/2021','10/29/2021','11/10/2021' )

        GROUP BY UTILS.CONVERT_TO_VARCHAR2(Process_date,200,p_style=>105),SourceName,AssetClassName
        ORDER BY UTILS.CONVERT_TO_VARCHAR2(Process_date,200,p_style=>105),
                 SourceName,
                 AssetClassName ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

