  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."ACBUSEGMENTMASTER_HISTORY" 
--exec [ACBUSegmentMaster_History] '1000'

(
  --declare --@ACBUSegmentCode Varchar(20)--=1506
  v_AcBuSegmentAlt_Key IN NUMBER,
  v_SourceName IN VARCHAR2--='Finacle'
)
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT 'AccountBusinessSegmentMaster' TableName  ,
             A.SourceAlt_Key ,
             SourceName ,
             ACBUSegmentCode ,
             ACBUSegmentDescription ,
             AcBuRevisedSegmentCode ,
             A.AuthorisationStatus ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifyBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  
        FROM DimAcBuSegment A
               LEFT JOIN DIMSOURCEDB B   ON A.SourceAlt_Key = B.SourceAlt_Key
       WHERE  A.EffectiveFromTimeKey <= v_TimeKey
                AND A.EffectiveToTimeKey >= v_TimeKey

                --AND				A.ACBUSegmentCode=@ACBUSegmentCode
                AND AcBuSegmentAlt_Key = v_AcBuSegmentAlt_Key
                AND B.sourceName = v_SourceName
      UNION 
      SELECT 'AccountBusinessSegmentMaster' TableName  ,
             A.SourceAlt_Key ,
             SourceName ,
             ACBUSegmentCode ,
             ACBUSegmentDescription ,
             AcBuRevisedSegmentCode ,
             A.AuthorisationStatus ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifyBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  
        FROM DimAcBuSegment_Mod A
               LEFT JOIN DIMSOURCEDB B   ON A.SourceAlt_Key = B.SourceAlt_Key
       WHERE  A.EffectiveFromTimeKey <= v_TimeKey
                AND A.EffectiveToTimeKey >= v_TimeKey

                --AND				A.ACBUSegmentCode=@ACBUSegmentCode
                AND AcBuSegmentAlt_Key = v_AcBuSegmentAlt_Key
                AND B.sourceName = v_SourceName ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

