  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."INDUSTRYHISTORY" 
(
  v_Code IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.IndustryMappingAlt_Key ,--as Code,

             A.IndustryName ,--as IndustryName,

             S.SourceAlt_Key ,
             A.SrcSysIndustryCode ,
             A.SrcSysIndustryName ,
             A.IndustryAlt_Key ,
             S.SourceName ,
             A.CreatedBy ,
             'IndustryHistory' TableName  ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModifie,20,p_style=>103) DateModified  
        FROM DimIndustryMapping A
               JOIN DIMSOURCEDB S   ON S.SourceAlt_Key = A.SourceAlt_Key
       WHERE  A.SrcSysIndustryCode = v_Code ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

