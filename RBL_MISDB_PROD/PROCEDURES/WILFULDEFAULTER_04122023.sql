(
  v_ReportingBank IN VARCHAR2 DEFAULT ' ' ,
  v_CustomerID IN VARCHAR2 DEFAULT ' ' ,
  v_PartyName IN VARCHAR2 DEFAULT ' ' ,
  v_PAN IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT ReportingBankFIAlt_Key ,
                B.BankName ReportedBank  ,
                ReportingBranchAlt_Key ,
                StateUTofBranchAlt_Key ,
                CustomerID ,
                PartyName ,
                PAN ,
                ReportingSerialNo ,
                OSAmountinlacs ,
                SuitFiledorNotAlt_Key ,
                OtherBanksFIInvolvedAlt_Key ,
                CustomerTypeAlt_Key --As CustomerType,

           FROM WillfulDefaulters A
                --Inner Join (Select ParameterAlt_Key,ParameterName,'Reportedby' as Tablename 
                 --		  from DimParameter where DimParameterName='Reportedby'
                 --		  And EffectiveFromTimeKey<=@TimeKey And EffectiveToTimeKey>=@TimeKey)B
                 --		  ON A.ReportedByAlt_Key=B.ParameterAlt_Key

                  JOIN DIMBANK B   ON B.BankAlt_Key = A.ReportedByAlt_Key
                  AND B.EffectiveFromTimeKey <= v_TimeKey
                  AND B.EffectiveToTimeKey >= v_TimeKey
          WHERE  A.EffectiveFromTimeKey <= v_TimeKey
                   AND A.EffectiveToTimeKey >= v_TimeKey
                   AND v_ReportingBank = v_ReportingBank
                   OR v_CustomerID = v_CustomerID
                   OR v_PartyName = v_PartyName
                   OR v_PAN = v_PAN ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

