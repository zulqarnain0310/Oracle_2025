  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."ADHOCASSETCLASSVIEWDETAIL_BACKUP_12122021" 
(
  v_UCIF_ID IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_TimeKey NUMBER(10,0);
   --SET @Timekey =(Select LastMonthDateKey from SysDayMatrix where Timekey=@Timekey) 
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT TimeKey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT DISTINCT UCIF_ID ,
                      A.AssetClassAlt_Key ,
                      B.AssetClassName AssetClass  ,
                      UTILS.CONVERT_TO_VARCHAR2(NPA_Date,20,p_style=>103) NPADate  ,
                      L.ParameterName MOCReason  ,
                      UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) MOCDate  ,
                      A.CreatedBy MOCBy  ,
                      UTILS.CONVERT_TO_VARCHAR2(FirstLevelDateApproved,20,p_style=>103) Level1ApprovedDate  ,
                      FirstLevelApprovedBy Level1ApprovedBy  ,
                      UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) Level2ApprovedDate  ,
                      A.ApprovedBy Level2ApprovedBy  ,
                      A.ModifyBy ,
                      UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  ,
                      A.AuthorisationStatus ,
                      A.ChangeType 
        FROM RBL_MISDB_PROD.AdhocACL_ChangeDetails_Mod A
               LEFT JOIN DimAssetClass B   ON A.AssetClassAlt_Key = B.AssetClassAlt_Key
               LEFT JOIN ( SELECT ParameterAlt_Key ,
                                  ParameterName ,
                                  'ModeOfOperationMaster' Tablename  
                           FROM DimParameter 
                            WHERE  DimParameterName = 'DimMoRreason'
                                     AND EffectiveFromTimeKey <= v_TimeKey
                                     AND EffectiveToTimeKey >= v_TimeKey ) L   ON L.ParameterAlt_Key = A.Reason
       WHERE  A.EffectiveFromTimeKey <= v_Timekey --And A.EffectiveToTimeKey>=@Timekey

                AND A.AuthorisationStatus = 'A'
                AND UCIF_ID = v_UCIF_ID ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

