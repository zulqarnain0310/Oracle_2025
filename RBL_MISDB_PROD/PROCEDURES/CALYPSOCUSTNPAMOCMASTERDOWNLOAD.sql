---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 --CollateralMasterDownload

AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT ParameterName ,
             'MOCType' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'MOCType'
                AND EffectiveFromTimeKey <= v_Timekey
                AND EffectiveToTimeKey >= v_Timekey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT AssetClassAlt_Key ,
             AssetClassName ,
             CASE 
                  WHEN AssetClassName = 'SUB-STANDARD' THEN 'If  SUB-STANDARD then NPA Date Mandatory , date format DD/MM/YYYY'
                  WHEN AssetClassName = 'DOUBTFUL I' THEN 'If  DOUBTFUL I then NPA Date Mandatory , date format DD/MM/YYYY'
                  WHEN AssetClassName = 'LOS' THEN 'If  LOS I then NPA Date Mandatory , date format DD/MM/YYYY'
             ELSE ' '
                END NPADate  ,
             'AssetClass' Tablename  
        FROM DimAssetClass 
       WHERE  EffectiveFromTimeKey <= v_Timekey
                AND EffectiveToTimeKey >= v_Timekey
                AND AssetClassAlt_Key NOT IN ( 4,5 )
    ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT MOCTypeName ,
             'MOCSource' TableName  
        FROM DimMOCType 
       WHERE  EffectiveFromTimeKey <= v_Timekey
                AND EffectiveToTimeKey >= v_Timekey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   --select 
   --			 SourceName as SourceSystem
   --			 ,'SourceSystem' as TableName
   --			 from DIMSOURCEDB
   --			 where EffectiveFromTimeKey<=@Timekey And EffectiveToTimeKey>=@Timekey
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             'MOCReason' TableName  
        FROM DimParameter 
       WHERE  EffectiveFromTimeKey <= v_Timekey
                AND EffectiveToTimeKey >= v_Timekey
                AND DimParameterName = 'DimMOCReason' ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

