  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."INSTRUMENTHISTORY" 
(
  v_Code IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.InstrumentTypeMappingAlt_Key ,
             A.InstrumentTypeName ,
             A.InstrumentTypeAlt_Key ,
             S.SourceAlt_Key ,
             A.SrcSysInstrumentTypeCode ,
             A.SrcSysInstrumentTypeName ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModifie,20,p_style=>103) DateModified  
        FROM DimInstrumentTypeMapping A
               JOIN DIMSOURCEDB S   ON S.SourceAlt_Key = A.SourceAlt_Key
       WHERE  A.SrcSysInstrumentTypeCode = v_Code ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

