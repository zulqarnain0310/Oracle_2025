  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."REBUILDINDEX_MANDEEP_04122023" 
AS
   --SELECT * FROM tt_NEWTEMP_2
   v_MAX NUMBER(10,0);
   v_MIN NUMBER(10,0);
   v_CONTENT NVARCHAR2(2000);
   v_INDEX NVARCHAR2(2000);
   v_TABLE NVARCHAR2(2000);
   v_cursor SYS_REFCURSOR;

BEGIN

   TABLE IF  --SQLDEV: NOT RECOGNIZED
   IF tt_temp_225  --SQLDEV: NOT RECOGNIZED
   DELETE FROM tt_temp_225;
   UTILS.IDENTITY_RESET('tt_temp_225');

   INSERT INTO tt_temp_225 ( 
   	SELECT * 
   	  FROM sys.tables 
   	 WHERE  /*TODO:SQLDEV*/ OBJECTPROPERTY(object_id,'Isindexed') /*END:SQLDEV*/ = 1 );
   IF  --SQLDEV: NOT RECOGNIZED
   IF tt_NEWTEMP_2  --SQLDEV: NOT RECOGNIZED
   DELETE FROM tt_NEWTEMP_2;
   UTILS.IDENTITY_RESET('tt_NEWTEMP_2');

   INSERT INTO tt_NEWTEMP_2 SELECT A.OBJECT_ID ,
                                   b.* ,
                                   ROW_NUMBER() OVER ( PARTITION BY B.TABLE_CATALOG ORDER BY B.TABLE_CATALOG  ) SNO  
        FROM tt_temp_225 A
               JOIN INFORMATION_SCHEMA.TABLES b   ON b.TABLE_NAME = A.NAME
       WHERE  b.TABLE_SCHEMA NOT IN ( 'dbo','PreMoc' )
   ;
   SELECT MAX(SNO)  

     INTO v_MAX
     FROM tt_NEWTEMP_2 ;
   v_MIN := 11 ;
   WHILE v_MIN <= v_MAX 
   LOOP 

      BEGIN
         SELECT TABLE_SCHEMA || '.' || TABLE_NAME 

           INTO v_TABLE
           FROM tt_NEWTEMP_2 
          WHERE  SNO = v_MIN;
         --SELECT @TABLE
         SELECT NAME 

           INTO v_INDEX
           FROM sys.indexes_ 
          WHERE  OBJECT_ID = ( SELECT OBJECT_ID 
                               FROM tt_NEWTEMP_2 
                                WHERE  SNO = v_MIN )
                   AND TYPE IN ( 1,2 )

           FETCH FIRST 1 ROWS ONLY;
         --SELECT @INDEX
         v_CONTENT := 'ALTER INDEX ' || v_INDEX || ' ON ' || v_TABLE || ' REBUILD ' ;
         OPEN  v_cursor FOR
            SELECT v_CONTENT 
              FROM DUAL  ;
            DBMS_SQL.RETURN_RESULT(v_cursor);
         --SP_UPDATESTATS
         --EXEC sp_executesql @CONTENT
         INSERT INTO RebuildIndex
           ( CONTENT )
           ( SELECT v_CONTENT 
               FROM DUAL  );
         v_MIN := v_MIN + 1 ;

      END;
   END LOOP;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

