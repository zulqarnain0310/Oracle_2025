  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."BUYOUTDOWNLOADDATA_ENQUIRY" 
(
  v_ExcelUploadId IN NUMBER
)
AS
   v_cursor SYS_REFCURSOR;
    v_Timekey NUMBER(10,0);
BEGIN

   DECLARE
      
   ----DECLARE @Timekey INT=49999
   ----	,@UserLoginId VARCHAR(100)='FNASUPERADMIN'
   ----	,@ExcelUploadId INT=4
   ----	,@UploadType VARCHAR(50)='Interest reversal'

   BEGIN
      SELECT UTILS.CONVERT_TO_NUMBER(B.timekey,10,0) 

        INTO v_Timekey
        FROM SysDataMatrix A
               JOIN SysDayMatrix B   ON A.TimeKey = B.TimeKey
       WHERE  A.CurrentStatus = 'C';
      DBMS_OUTPUT.PUT_LINE(v_Timekey);

   END;

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Details' TableName  ,
                UploadID ,
                --,SummaryID
                SlNo ,
                AUNo ,
                PoolName ,
                Category ,
                BuyoutPartyLoanNo ,
                CustomerName ,
                PAN ,
                AadharNo ,
                PrincipalOutstanding ,
                InterestReceivable ,
                Charges ,
                AccuredInterest ,
                DPD ,
                AssetClass 

           ------,ROW_NUMBER() OVER (ORDER BY (SELECT 1)) AS ROW_NUM
           FROM BuyoutDetails_Mod 
          WHERE  UploadId = v_ExcelUploadId
                   AND EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      OPEN  v_cursor FOR
         SELECT 'Summary' TableName  ,
                ROW_NUMBER() OVER ( ORDER BY AUNo  ) SrNo  ,
                UploadID ,
                SummaryID ,
                AUNo ,
                PoolName ,
                Category ,
                TotalNoofBuyoutParty ,
                TotalPrincipalOutstandinginRs ,
                TotalInterestReceivableinRs ,
                BuyoutOSBalanceinRs ,
                TotalChargesinRs ,
                TotalAccuredInterestinRs 

           ------,ROW_NUMBER() OVER (ORDER BY (SELECT 1)) AS ROW_NUM
           FROM BuyoutSummary_Mod 
          WHERE  UploadId = v_ExcelUploadId
                   AND EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

