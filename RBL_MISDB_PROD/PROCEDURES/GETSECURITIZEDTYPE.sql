set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."GETSECURITIZEDTYPE" 
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT MAX(Timekey)  

     INTO v_Timekey
     FROM RBL_MISDB_PROD.SysDayMatrix 
    WHERE  Date_ = UTILS.CONVERT_TO_VARCHAR2(SYSDATE,200);
   DBMS_OUTPUT.PUT_LINE(v_Timekey);
   OPEN  v_cursor FOR
      SELECT 'SecuritizedType' TableName  ,
             ParameterAlt_Key ,
             ParameterName 
        FROM DimParameter 
       WHERE  DimParameterName = 'Securitized'
                AND EffectiveFromTimeKey <= v_Timekey
                AND EffectiveToTimeKey >= v_Timekey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

/

