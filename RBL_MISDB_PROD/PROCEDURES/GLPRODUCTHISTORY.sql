  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."GLPRODUCTHISTORY" 
(
  v_ProductCode IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.GLProductAlt_Key ,
             B.ProductCode ,
             B.ProductName ,
             C.SourceName ,
             C.SourceAlt_Key ,
             D.ParameterName FacilityType  ,
             A.AssetGLCode_STD ,
             A.InterestReceivableGLCode_STD ,
             A.InerestAccruedGLCode_STD ,
             A.InterestIncome_STD ,
             A.SuspendedAssetGLCode_NPA ,
             A.SuspendedInterestReceivableGLCode_NPA ,
             A.SuspendedInterestAccruedGLCode_NPA ,
             A.SuspendedInterestIncomeGLCode_NPA ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  
        FROM DimGLProduct_AU A
               JOIN DimProduct B   ON A.ProductCode = B.ProductCode
               JOIN DIMSOURCEDB C   ON A.SourceAlt_key = C.SourceAlt_Key
               JOIN ( SELECT ParameterAlt_Key ,
                             ParameterName ,
                             'Facility Type' Tablename  
                      FROM DimParameter 
                       WHERE  DimParameterName = 'DimGLProduct' ) D   ON D.ParameterAlt_Key = A.FacilityTypeAlt_Key
       WHERE  A.ProductCode = v_ProductCode ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

