   BEGIN
      OPEN  v_cursor FOR
         SELECT AssetClassAlt_Key ,
                AssetClassName 
           FROM DimAssetClass 
          WHERE  AssetClassAlt_Key IN ( 4,6 )
       ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

