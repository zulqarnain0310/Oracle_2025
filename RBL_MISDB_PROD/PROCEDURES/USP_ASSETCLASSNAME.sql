   END IF;
   IF ( v_AssetClassAlt_Key = 6 ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT AssetClassAlt_Key ,
                AssetClassName 
           FROM DimAssetClass 
          WHERE  AssetClassAlt_Key IN ( 5 )
       ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

