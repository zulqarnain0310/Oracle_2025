  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."RPT_MONTH" 
(
  v_Year IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT DISTINCT utils.datename('MM', DATE_) MonthName  ,
                      utils.datepart('MM', DATE_) orderby  
        FROM SysDayMatrix 
       WHERE  Date_ <= SYSDATE
                AND utils.year_(Date_) = v_Year
        ORDER BY orderby DESC ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

/

