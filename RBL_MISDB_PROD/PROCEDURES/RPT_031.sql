  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."RPT_031" 
(
  v_FromDate IN VARCHAR2,
  v_ToDate IN VARCHAR2,
  v_SourceSystem IN VARCHAR2
)
AS
   --DECLARE  @FromDate AS VARCHAR(20)='01/05/2021',
   --         @ToDate   AS VARCHAR(20)='31/05/2021',
   --		 @SourceSystem AS VARCHAR(200)='Finacle'
   v_From1 VARCHAR2(200) := ( SELECT Rdate 
     FROM TABLE(DateConvert(v_FromDate))  );
   v_To1 VARCHAR2(200) := ( SELECT Rdate 
     FROM TABLE(DateConvert(v_ToDate))  );
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT UCIC_Code ,
             CustomerID CIF_ID  ,
             CustomerName Customer_Name  ,
             AccountNo Account_No  ,
             Host_System_Name ,
             SUM(NVL(OSBalance, 0))  OS_Balance  ,
             UTILS.CONVERT_TO_VARCHAR2(Report_Date,20,p_style=>103) Report_Date  ,
             Account_Level_Business_Segment ,
             Business_Seg_Desc ,
             Base_Account_Scheme_Code ,
             Base_Account_Scheme_Owner ,
             Host_System_Status ,
             Remarks ,
             UTILS.CONVERT_TO_VARCHAR2(Closed_Date,20,p_style=>103) Closed_Date  ,
             Cr_Dr 
        FROM RBL_MISDB_PROD.HostSystemStatus 
       WHERE  UTILS.CONVERT_TO_VARCHAR2(Report_Date,200) BETWEEN v_From1 AND v_To1
                AND Host_System_Name IN ( SELECT * 
                                          FROM TABLE(SPLIT(v_SourceSystem, ','))  )

        GROUP BY UCIC_Code,CustomerID,CustomerName,AccountNo,Host_System_Name,Report_Date,Closed_Date,Cr_Dr,Account_Level_Business_Segment,Business_Seg_Desc,Base_Account_Scheme_Code,Base_Account_Scheme_Owner,Host_System_Status,Remarks ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

/

