(
  v_MenuID IN NUMBER DEFAULT 10 ,
  v_UserLoginId IN VARCHAR2 DEFAULT 'FnaAdmin' ,
  iv_Timekey IN NUMBER DEFAULT 49999 ,
  v_UploadID IN NUMBER
)
AS
   v_Timekey NUMBER(10,0) := iv_Timekey;
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN

      BEGIN
         /*TODO:SQLDEV*/ SET DATEFORMAT DMY /*END:SQLDEV*/
         SELECT MAX(Timekey)  

           INTO v_Timekey
           FROM SysDayMatrix 
          WHERE  UTILS.CONVERT_TO_VARCHAR2(date_,200) = UTILS.CONVERT_TO_VARCHAR2(SYSDATE,200);
         DBMS_OUTPUT.PUT_LINE(v_Timekey);
         IF ( v_MenuID = '1462' ) THEN

         BEGIN
            OPEN  v_cursor FOR
               SELECT --Row_Number() over(order by PoolID) as SrNo ,
                      NoofAccounts ,
                      TotalPOSinRs ,
                      TotalInttReceivableinRs ,
                      TotaloutstandingBalanceinRs ,
                      ExposuretoARCinRs ,
                      UTILS.CONVERT_TO_VARCHAR2(DateOfSaletoARC,20,p_style=>103) DateOfSaletoARC  ,
                      UTILS.CONVERT_TO_VARCHAR2(DateOfApproval,20,p_style=>103) DateOfApproval  
                 FROM ( SELECT
                        --Count(1) NoofAccounts
                         --NoofAccounts
                         --,SUM(TotalPOSinRs)TotalPOSinRs
                         --,SUM(TotalInttReceivableinRs)TotalInttReceivableinRs
                         --,SUM(TotaloutstandingBalanceinRs)TotaloutstandingBalanceinRs
                         --,SUM(ExposuretoARCinRs)ExposuretoARCinRs
                         --,MAX(DateOfSaletoARC)DateOfSaletoARC
                         --,MAX(DateOfApproval)DateOfApproval
                         NoofAccounts ,
                         TotalPOSinRs ,
                         TotalInttReceivableinRs ,
                         TotaloutstandingBalanceinRs ,
                         ExposuretoARCinRs ,
                         DateOfSaletoARC ,
                         DateOfApproval 
                        FROM SaletoARCSummary_Mod 
                         WHERE --Isnull(AuthorisationStatus,'A') in ('NP','MP') And
                          EffectiveFromTimeKey <= v_TimeKey
                            AND EffectiveToTimeKey >= v_TimeKey
                            AND UploadID = v_UploadID ) 
                      --Group By 

                      --PoolID,PoolName,PoolType
                      A ;
               DBMS_SQL.RETURN_RESULT(v_cursor);

         END;
         END IF;

      END;
   EXCEPTION
      WHEN OTHERS THEN

   BEGIN
      INSERT INTO RBL_MISDB_PROD.Error_Log
        ( SELECT utils.error_line ErrorLine  ,
                 SQLERRM ErrorMessage  ,
                 SQLCODE ErrorNumber  ,
                 utils.error_procedure ErrorProcedure  ,
                 utils.error_severity ErrorSeverity  ,
                 utils.error_state ErrorState  ,
                 SYSDATE 
            FROM DUAL  );

   END;END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

