  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."VALUATIONSOURCEDROPDOWN_310872023" 
-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

(
  v_CollateralID IN VARCHAR2 DEFAULT ' ' ,
  iv_CollateralTypeAlt_Key IN NUMBER DEFAULT 0 
)
AS
   v_CollateralTypeAlt_Key NUMBER(10,0) := iv_CollateralTypeAlt_Key;
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT TimeKey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   IF v_CollateralID = ' ' THEN

   BEGIN
      SELECT CollateralSubTypeAlt_Key 

        INTO v_CollateralTypeAlt_Key
        FROM CurDat_RBL_MISDB_PROD.AdvSecurityDetail 
       WHERE  CollateralID = v_CollateralID;
      IF v_CollateralTypeAlt_Key = 0 THEN

      BEGIN
         SELECT CollateralSubTypeAlt_Key 

           INTO v_CollateralTypeAlt_Key
           FROM RBL_MISDB_PROD.AdvSecurityDetail_Mod 
          WHERE  CollateralID = v_CollateralID;

      END;
      END IF;

   END;
   END IF;

   BEGIN
      OPEN  v_cursor FOR
         SELECT ValueExpirationAltKey SecuritySubTypeAlt_Key  ,
                Documents ParameterName  ,
                ExpirationPeriod PeriodInMonth  ,
                'ValuationSource' TableName  
           FROM DimValueExpiration 
          WHERE  EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey
                   AND SecurityTypeAlt_Key = v_CollateralTypeAlt_Key ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      OPEN  v_cursor FOR
         SELECT A.SecurityTypeAlt_Key CollateralType_AltKey  ,
                B.CollateralTypeDescription CollateralType  ,
                A.SecuritySubTypeAlt_Key SecuritySubTypeAlt_Key  ,
                C.CollateralSubTypeDescription ParameterName  ,
                A.ExpirationPeriod PeriodInMonth  ,
                'ValuationSourceData' TableName  
           FROM DimValueExpiration A
                  JOIN DimCollateralType B   ON A.SecurityTypeAlt_Key = B.CollateralTypeAltKey
                  JOIN DimCollateralSubType C   ON A.SecurityTypeAlt_Key = C.CollateralTypeAltKey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

