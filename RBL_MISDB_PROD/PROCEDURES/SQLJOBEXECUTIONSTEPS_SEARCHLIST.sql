  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SQLJOBEXECUTIONSTEPS_SEARCHLIST" 
(
  v_JobName IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_cursor SYS_REFCURSOR;
--,@Step_No				int         	= 0
--DECLARE @JobName VARCHAR(100)='TestNewJOB'

BEGIN

   OPEN  v_cursor FOR
      SELECT A.job_id Job_Id  ,
             A.Name Job_Name  ,
             b.step_id Step_Id  ,
             (UTILS.CONVERT_TO_VARCHAR2(b.step_id,30) || '. ' || b.step_name) ACLJobStepNumber  ,
             'JobStepDetails' TableName  
        FROM MSDB.sysjobs a
               JOIN msdb.sysjobsteps b   ON a.job_id = b.job_id
       WHERE  NAME = v_JobName
        ORDER BY b.step_id ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

