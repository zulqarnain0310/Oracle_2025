  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SECURITIZEDDOWNLOADDATA" 
(
  iv_Timekey IN NUMBER,
  v_UserLoginId IN VARCHAR2,
  v_ExcelUploadId IN NUMBER,
  v_UploadType IN VARCHAR2
)
AS
   v_Timekey NUMBER(10,0) := iv_Timekey;
   --SELECT * FROM(
   v_cursor SYS_REFCURSOR;
--,@Page SMALLINT =1     
--   ,@perPage INT = 30000   
----DECLARE @Timekey INT=49999
----	,@UserLoginId VARCHAR(100)='FNASUPERADMIN'
----	,@ExcelUploadId INT=4
----	,@UploadType VARCHAR(50)='Interest reversal'

BEGIN

   SELECT UTILS.CONVERT_TO_NUMBER(B.timekey,10,0) 

     INTO v_Timekey
     FROM SysDataMatrix A
            JOIN SysDayMatrix B   ON A.TimeKey = B.TimeKey
    WHERE  A.CurrentStatus = 'C';
   DBMS_OUTPUT.PUT_LINE(v_Timekey);
   --DECLARE @PageFrom INT, @PageTo INT   
   --SET @PageFrom = (@perPage*@Page)-(@perPage) +1  
   --SET @PageTo = @perPage*@Page  
   IF ( v_UploadType = 'Securitized Upload' ) THEN

   BEGIN
      DBMS_OUTPUT.PUT_LINE('REV');
      OPEN  v_cursor FOR
         SELECT 'Details' TableName  ,
                -- ,SrNo
                UploadID ,
                --,SummaryID
                PoolID ,
                PoolName ,
                SecuritisationType ,
                CustomerID ,
                AccountID ,
                POS ,
                InterestReceivable ,
                OSBalance ,
                SecuritisationExposureinRs ,
                UTILS.CONVERT_TO_VARCHAR2(DateofSecuritisationreckoning,20,p_style=>103) DateofSecuritisationreckoning  ,
                UTILS.CONVERT_TO_VARCHAR2(DateofSecuritisationmarking,20,p_style=>103) DateofSecuritisationmarking  ,
                UTILS.CONVERT_TO_VARCHAR2(MaturityDate,20,p_style=>103) MaturityDate  

           ------,ROW_NUMBER() OVER (ORDER BY (SELECT 1)) AS ROW_NUM
           FROM SecuritizedDetail_MOD 
          WHERE  UploadId = v_ExcelUploadId
                   AND EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      OPEN  v_cursor FOR
         SELECT 'Summary' TableName  ,
                ROW_NUMBER() OVER ( ORDER BY PoolID  ) SrNo  ,
                UploadID ,
                SummaryID ,
                PoolID ,
                PoolName ,
                SecuritisationType ,
                POS ,
                SecuritisationExposureAmt ,
                UTILS.CONVERT_TO_VARCHAR2(SecuritisationReckoningDate,20,p_style=>103) SecuritisationReckoningDate  ,
                UTILS.CONVERT_TO_VARCHAR2(SecuritisationMarkingDate,20,p_style=>103) SecuritisationMarkingDate  ,
                --,SecuritisationPortfolio
                UTILS.CONVERT_TO_VARCHAR2(MaturityDate,20,p_style=>103) MaturityDate  

           ------,ROW_NUMBER() OVER (ORDER BY (SELECT 1)) AS ROW_NUM
           FROM SecuritizedSummary_Mod 
          WHERE  UploadId = v_ExcelUploadId
                   AND EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);--)A
      --WHERE ROW_NUM BETWEEN  @PageFrom AND @PageTo
      --ORDER BY ROW_NUM  

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

