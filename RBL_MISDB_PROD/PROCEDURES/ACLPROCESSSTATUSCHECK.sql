  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."ACLPROCESSSTATUSCHECK" 
AS
   --DECLARE @TIMEKEY INT=(SELECT Timekey FROM Automate_Advances WHERE EXT_FLG='Y')
   v_ProcessDate VARCHAR2(200) ;
   v_TIMEKEY NUMBER(10,0) ;
   v_ACLTimekey NUMBER(10,0);
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

      SELECT DISTINCT date_of_data INTO v_ProcessDate 
     FROM RBL_STGDB.account_data_finacle  ;
     
   SELECT Timekey INTO v_TIMEKEY
     FROM Automate_Advances 
    WHERE  UTILS.CONVERT_TO_VARCHAR2(Date_,200) = v_ProcessDate ;
    
   SELECT MAX(timekey)  INTO v_ACLTimekey 
     FROM ACLProcessInProgressStatus;
   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE EXISTS ( SELECT 1 
                      FROM ACLProcessInProgressStatus 
                       WHERE  STATUS = 'RUNNING'
                                AND StatusFlag = 'N'
                                AND TimeKey = v_TimeKey );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      DBMS_OUTPUT.PUT_LINE('ACL Process is In Progress');
      OPEN  v_cursor FOR
         SELECT 
                 SR_NO	,
                PROCESS_DATE	,
                RUN_DATE	,
                EXECUTIONSTARTTIME	,
                EXECUTIONENDTIME	,
                STEPDESCRIPTION	,
                STATUS	,
                STATUSFLAG	,
                TIMEKEY	,
                'ACLProcessingStatus' TABLENAME	
           FROM ACLProcessInProgressStatus 
          WHERE  STATUS = 'RUNNING'
                   AND StatusFlag = 'N'
                   AND TimeKey = v_TimeKey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;
   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE EXISTS ( SELECT 1 
                      FROM ACLProcessInProgressStatus 
                       WHERE  STATUS = 'COMPLETED'
                                AND StatusFlag = 'Y'
                                AND TimeKey = v_TimeKey );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      DBMS_OUTPUT.PUT_LINE('ACL Process Completed');
      OPEN  v_cursor FOR
         SELECT  SR_NO	,
                PROCESS_DATE	,
                RUN_DATE	,
                EXECUTIONSTARTTIME	,
                EXECUTIONENDTIME	,
                STEPDESCRIPTION	,
                STATUS	,
                STATUSFLAG	,
                TIMEKEY	,
                'ACLProcessingStatus' TABLENAME	
           FROM ACLProcessInProgressStatus 
          WHERE  STATUS = 'COMPLETED'
                   AND StatusFlag = 'Y'
                   AND TimeKey = v_TimeKey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

   ---------------------Added by Sudesh 25102023--------------Screen Freeze Issue------------------
   ELSE
      IF ( v_TIMEKEY > v_ACLTimekey ) THEN

      BEGIN
         DBMS_OUTPUT.PUT_LINE('ACL Process Completed');
         OPEN  v_cursor FOR
            SELECT  SR_NO	,
                PROCESS_DATE	,
                RUN_DATE	,
                EXECUTIONSTARTTIME	,
                EXECUTIONENDTIME	,
                STEPDESCRIPTION	,
                STATUS	,
                STATUSFLAG	,
                TIMEKEY	,
                'ACLProcessingStatus' TABLENAME	
              FROM ACLProcessInProgressStatus 
             WHERE  STATUS = 'COMPLETED'
                      AND StatusFlag = 'Y'
                      AND TimeKey = v_ACLTimekey ;
            DBMS_SQL.RETURN_RESULT(v_cursor);

      END;
      END IF;
   END IF;

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

