set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SP_TOKENDETAILS_INUP" 
(
  v_TokenID IN VARCHAR2
)
AS
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE 0 = ( SELECT COUNT(TokenStatus)  
                   FROM TokenDetails 
                    WHERE  TokenStatus = 0 );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      INSERT INTO TokenDetails
        ( TokenID, TokenStatus, CreationDate )
        VALUES ( v_TokenID, 0, TO_DATE(SYSDATE,'dd/mm/yyyy') );

   END;
   END IF;
   OPEN  v_cursor FOR
      SELECT TokenID 
        FROM TokenDetails 
       WHERE  TokenStatus = 0 ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

