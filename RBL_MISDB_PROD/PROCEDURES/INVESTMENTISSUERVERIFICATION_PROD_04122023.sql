(
  v_IssuerId IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_TimeKey NUMBER(10,0);
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE NOT EXISTS ( SELECT 1 
                          FROM CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail A
                           WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                                    AND A.EffectiveToTimeKey >= v_TimeKey )
                                    AND A.IssuerId = v_IssuerId
                                    AND NVL(A.AuthorisationStatus, 'A') = 'A' );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      DELETE FROM tt_temp1_11;
      UTILS.IDENTITY_RESET('tt_temp1_11');

      INSERT INTO tt_temp1_11 ( 
      	SELECT * 
      	  FROM CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail A
      	 WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                 AND A.EffectiveToTimeKey >= v_TimeKey )
                 AND A.IssuerId = v_IssuerId
                 AND NVL(A.AuthorisationStatus, 'A') = 'A' );
      OPEN  v_cursor FOR
         SELECT ( SELECT ROW_NUMBER() OVER ( ORDER BY IssuerEntityID  ) RowNumber  
                    FROM DUAL  ) ,
                COUNT(*) OVER ( ) TotalCount  ,
                'InvestmentCodeMaster' TableName  ,
                * 
           FROM tt_temp1_11  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   ELSE

   BEGIN
      DELETE FROM tt_temp_179;
      UTILS.IDENTITY_RESET('tt_temp_179');

      INSERT INTO tt_temp_179 ( 
      	SELECT * 
      	  FROM CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail A
      	 WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                 AND A.EffectiveToTimeKey >= v_TimeKey )
                 AND A.IssuerId = v_IssuerId
                 AND NVL(A.AuthorisationStatus, 'A') = 'A' );
      OPEN  v_cursor FOR
         SELECT ( SELECT ROW_NUMBER() OVER ( ORDER BY IssuerEntityID  ) RowNumber  
                    FROM DUAL  ) ,
                COUNT(*) OVER ( ) TotalCount  ,
                'InvestmentCodeMaster' TableName  ,
                * 
           FROM tt_temp_179  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

