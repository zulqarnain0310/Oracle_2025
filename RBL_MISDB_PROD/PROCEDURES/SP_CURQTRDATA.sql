set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SP_CURQTRDATA" 
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.UCIF_ID ,
             B.RefCustomerID CustomerID  ,
             CustomerName ,
             CustomerAcID ,
             Balance ,
             NVL(CurQtrInt, 0) CurQtrInt  ,
             NVL(CurQtrCredit, 0) CurQtrCredit  ,
             NVL(CurQtrInt, 0) - NVL(CurQtrCredit, 0) DIFFERENCE  ,
             DebitSinceDt 
        FROM PRO_RBL_MISDB_PROD.CUSTOMERCAL A
               JOIN PRO_RBL_MISDB_PROD.ACCOUNTCAL B   ON A.CustomerEntityID = B.CustomerEntityID
               JOIN DimProduct C   ON B.ProductAlt_Key = C.ProductAlt_Key
       WHERE  SchemeType = 'ODA' ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

