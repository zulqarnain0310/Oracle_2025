  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."PROVISIONHISTORY" 
(
  v_ProvisionName IN VARCHAR2
)
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT 'ProvisionHistory' TableName  ,
             A.ProvisionAlt_Key Code  ,
             A.BankCategoryID ,
             A.ProvisionName AssetCategory  ,
             B.ParameterName CategoryType  ,
             A.CategoryTypeAlt_Key ,
             A.ProvisionSecured ProvisionPrcntRBINorms  ,
             A.AdditionalBanksProvision ,
             A.AdditionalprovisionRBINORMS AdditionalProvisionPrcntBankNorms  ,
             CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(DateCreated,20,p_style=>103) DateCreated  ,
             ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(DateApproved,20,p_style=>103) DateApproved  ,
             ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(DateModified,20,p_style=>103) DateModified  ,
             UTILS.CONVERT_TO_VARCHAR2(S1.Date_,20,p_style=>103) EffectiveFromDate  ,
             UTILS.CONVERT_TO_VARCHAR2(S2.Date_,20,p_style=>103) EffectiveToDate  
        FROM DimProvision_SegStd A
               JOIN SysDayMatrix S1   ON S1.TimeKey = A.EffectiveFromTimeKey
               JOIN SysDayMatrix S2   ON S2.TimeKey = A.EffectiveToTimeKey
               JOIN ( SELECT ParameterAlt_Key ,
                             ParameterName ,
                             'CategoryType' Tablename  
                      FROM DimParameter 
                       WHERE  DimParameterName = 'Category Type'
                                AND EffectiveFromTimeKey <= v_TimeKey
                                AND EffectiveToTimeKey >= v_TimeKey ) B   ON A.CategoryTypeAlt_Key = B.ParameterAlt_Key
       WHERE  ProvisionName = v_ProvisionName ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

