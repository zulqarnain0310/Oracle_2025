  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SP_REVERSEFEEDDATACOUNTRBL_04122023" 
AS
   v_TimeKey NUMBER(10,0) := ( SELECT TimeKey 
     FROM Automate_Advances 
    WHERE  EXT_FLG = 'Y' );

BEGIN

   DELETE ReverseFeedDataCount

    WHERE  ProcessDate IN ( SELECT Date_ 
                            FROM Automate_Advances 
                             WHERE  EXT_FLG = 'Y' )
   ;
   INSERT INTO ReverseFeedDataCount
     ( 
       -----------------Asset Classification---------
       SELECT DateofData ProcessDate  ,
              SYSDATE GenerationDate  ,
              'Asset Classification' NAME  ,
              SourceSystemName SourceName  ,
              COUNT(1)  COUNT  
       FROM ReverseFeedData 
        WHERE  EffectiveFromTimeKey <= v_Timekey
                 AND EffectiveToTimekey >= v_Timekey
         GROUP BY DateofData,SourceSystemName
       UNION 

       -----------------Degrade---------
       SELECT DateofData ProcessDate  ,
              SYSDATE GenerationDate  ,
              'Degrade' NAME  ,
              SourceSystemName SourceName  ,
              COUNT(1)  COUNT  
       FROM ReverseFeedData 
        WHERE  EffectiveFromTimeKey <= v_Timekey
                 AND EffectiveToTimekey >= v_Timekey
                 AND AssetSubClass <> 'STD'
         GROUP BY DateofData,SourceSystemName
       UNION 

       -----------------Upgrade---------
       SELECT DateofData ProcessDate  ,
              SYSDATE GenerationDate  ,
              'Upgrade' NAME  ,
              SourceSystemName SourceName  ,
              COUNT(1)  COUNT  
       FROM ReverseFeedData 
        WHERE  EffectiveFromTimeKey <= v_Timekey
                 AND EffectiveToTimekey >= v_Timekey
                 AND AssetSubClass = 'STD'
         GROUP BY DateofData,SourceSystemName );

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

/

