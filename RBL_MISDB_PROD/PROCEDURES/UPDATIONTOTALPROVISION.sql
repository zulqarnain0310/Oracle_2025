DESCRIPTION : UPDATE TOTAL PROVISION          
--EXEC [dbo].[UpdationTotalProvision] @TimeKey =1              
=========================================*/
(
  v_TimeKey IN NUMBER
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN

      BEGIN
         UPDATE RBL_MISDB_PROD.ACCOUNTCAL
            SET TotalProvision = 0;
         MERGE INTO A 
         USING (SELECT A.ROWID row_id, (CASE 
         WHEN ((NVL(A.Provsecured, 0) + NVL(A.ProvUnsecured, 0) + (NVL(A.DFVAmt, 0)))) > A.NetBalance THEN (A.NetBalance - (NVL(A.Provsecured, 0) + NVL(A.ProvUnsecured, 0)))   END) AS DFVAMT
         FROM A ,RBL_MISDB_PROD.ACCOUNTCAL A ) src
         ON ( A.ROWID = src.row_id )
         WHEN MATCHED THEN UPDATE SET A.DFVAMT = src.DFVAMT;
         MERGE INTO A 
         USING (SELECT A.ROWID row_id, (NVL(A.Provsecured, 0) + NVL(A.ProvUnsecured, 0) + (NVL(A.DFVAmt, 0)) + (NVL(A.AddlProvision, 0))) AS TotalProvision
         FROM A ,RBL_MISDB_PROD.ACCOUNTCAL A ) src
         ON ( A.ROWID = src.row_id )
         WHEN MATCHED THEN UPDATE SET TotalProvision = src.TotalProvision;
         MERGE INTO A 
         USING (SELECT A.ROWID row_id, B.TotalProvision
         FROM A ,RBL_MISDB_PROD.AdvAcBalanceDetail A
                JOIN RBL_MISDB_PROD.ACCOUNTCAL B   ON A.AccountEntityId = B.AccountEntityID
                AND ( A.EffectiveFromTimeKey <= v_TIMEKEY
                AND A.EffectiveToTimeKey >= v_TIMEKEY ) ) src
         ON ( A.ROWID = src.row_id )
         WHEN MATCHED THEN UPDATE SET A.TotalProv = src.TotalProvision;

      END;
   EXCEPTION
      WHEN OTHERS THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT SQLERRM ERROR_MESSAGE  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

