BEGIN

   SELECT MAX(Timekey)  

     INTO v_Timekey
     FROM RBL_MISDB_PROD.DimDayMatrix 
    WHERE  Date_ = UTILS.CONVERT_TO_VARCHAR2(SYSDATE,200);
   DBMS_OUTPUT.PUT_LINE(v_Timekey);
   OPEN  v_cursor FOR
      SELECT 'IBPCData' TableName  ,
             ROW_NUMBER() OVER ( ORDER BY PoolID  ) SrNo  ,
             UploadID ,
             SummaryID ,
             PoolID ,
             PoolName ,
             PoolType ,
             NoOfAccount ,
             BalanceOutstanding ,
             IBPCExposureAmt ,
