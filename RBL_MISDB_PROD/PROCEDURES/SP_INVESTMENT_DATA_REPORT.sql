--  DDL for Procedure SP_INVESTMENT_DATA_REPORT
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SP_INVESTMENT_DATA_REPORT" 
AS
   v_Date VARCHAR2(200) := ( SELECT Date_ 
     FROM Automate_Advances 
    WHERE  Ext_flg = 'Y' );
   v_cursor SYS_REFCURSOR;

BEGIN

   DELETE FROM tt_Investment_Data;
   UTILS.IDENTITY_RESET('tt_Investment_Data');

   INSERT INTO tt_Investment_Data ( 
   	SELECT * 
   	  FROM INVESTMENT_DATA 
   	 WHERE  UTILS.CONVERT_TO_VARCHAR2(process_Date,200,p_style=>105) = v_Date );
   DELETE FROM tt_UCIF_8;
   UTILS.IDENTITY_RESET('tt_UCIF_8');

   INSERT INTO tt_UCIF_8 ( 
   	SELECT UCIFID ,
           'Y' FLGDEG  
   	  FROM INVESTMENT_DATA 
   	 WHERE  ( FLGDEG = 'Y'
              OR ASSETCLASS <> 'STD' )
              AND UTILS.CONVERT_TO_VARCHAR2(process_Date,200,p_style=>105) = v_Date );
   MERGE INTO A 
   USING (SELECT A.ROWID row_id, 'Y'
   FROM A ,tt_Investment_Data a
          JOIN tt_UCIF_8 b   ON a.UCIFID = b.UCIFID ) src
   ON ( A.ROWID = src.row_id )
   WHEN MATCHED THEN UPDATE SET a.FLGDEG = 'Y';
   EXECUTE IMMEDIATE ' TRUNCATE TABLE Investment_Data_Report ';
   INSERT INTO Investment_Data_Report
     ( SELECT Process_Date ,
              BRANCHCODE ,
              ISSUERID ,
              ISSUERNAME ,
              REF_TXN_SYS_CUST_ID ,
              ISSUER_CATEGORY_CODE ,
              UCIFID UCICID  ,
              INVID ,
              ISIN ,
              INSTRUMENTTYPENAME ,
              INSTRNAME ,
              INVESTMENTNATURE ,
              EXPOSURETYPE ,
              MATURITYDT ,
              HOLDINGNATURE ,
              BOOKTYPE ,
              BOOKVALUE ,
              MTMVALUE ,
              INTEREST_DIVIDENDDUEDATE ,
              INTEREST_DIVIDENDDUEAMOUNT ,
              DPD ,
              FLGDEG Fresh_Degrade_Y_N_  ,
              DEGREASON ,
              FLGUPG ,
              UPGDATE ,
              TOTALPROVISON ,
              ASSETCLASS ,
              PartialRedumptionDueDate ,
              PartialRedumptionSettledY_N ,
              NPIDt ,
              BalanceSheetDate ,
              ListedShares ,
              DPD_BS_Date ,
              BookValueINR 

       --into Investment_Data_Report
       FROM tt_Investment_Data  );
   --where convert(date,process_Date,105) = @Date
   OPEN  v_cursor FOR
      SELECT Process_Date ,
             BRANCHCODE ,
             ISSUERID ,
             ISSUERNAME ,
             REF_TXN_SYS_CUST_ID ,
             ISSUER_CATEGORY_CODE ,
             UCICID ,
             INVID ,
             ISIN ,
             INSTRUMENTTYPENAME ,
             INSTRNAME ,
             INVESTMENTNATURE ,
             EXPOSURETYPE ,
             --,MATURITYDT
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(MATURITYDT,200,p_style=>103),10,p_style=>23) MATURITYDT  ,
             HOLDINGNATURE ,
             BOOKTYPE ,
             BOOKVALUE ,
             MTMVALUE ,
             INTEREST_DIVIDENDDUEDATE ,
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(NPIDt,200,p_style=>105),10,p_style=>23) NPIDt  ,
             INTEREST_DIVIDENDDUEAMOUNT ,
             DPD ,
             Fresh_Degrade_Y_N ,
             DEGREASON ,
             FLGUPG ,
             --,UPGDATE
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(UPGDATE,200,p_style=>105),10,p_style=>23) UPGDATE  ,
             TOTALPROVISON ,
             ASSETCLASS ,
             --,PartialRedumptionDueDate
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(PartialRedumptionDueDate,200,p_style=>105),10,p_style=>23) PartialRedumptionDueDate  ,
             PartialRedumptionSettledY_N ,
             --,NPIDt
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(NPIDt,200,p_style=>105),10,p_style=>23) NP_A5  ,
             --,BalanceSheetDate
             UTILS.CONVERT_TO_NVARCHAR2(UTILS.CONVERT_TO_VARCHAR2(BalanceSheetDate,200,p_style=>105),10,p_style=>23) BalanceSheetDate  ,
             ListedShares ,
             DPD_BS_Date ,
             --,convert(nvarchar(10),convert(date,DPD_BS_Date , 105),23) as DPD_BS_Date
             BookValueINR 
        FROM Investment_Data_Report  ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

