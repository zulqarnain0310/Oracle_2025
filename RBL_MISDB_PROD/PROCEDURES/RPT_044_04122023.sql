       WHERE  action_id <> 'SL'
                AND Server_principal_name <> 'S-1-9-3-36591511-1194892724-3338897577-844150044' ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
