BEGIN

   --SET @Timekey =(Select TimeKey from SysDataMatrix where CurrentStatus='C') 
   --SET @Timekey =(Select LastMonthDateKey from SysDayMatrix where Timekey=@Timekey) 
   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  MOC_Initialised = 'Y'
             AND NVL(MOC_Frozen, 'N') = 'N';
   DBMS_OUTPUT.PUT_LINE(v_Timekey);
   --DECLARE @PageFrom INT, @PageTo INT   
   --SET @PageFrom = (@perPage*@Page)-(@perPage) +1  
   --SET @PageTo = @perPage*@Page  
   IF ( v_UploadType = 'Customer MOC Upload' ) THEN

   BEGIN
      --SELECT * FROM(
      OPEN  v_cursor FOR
         SELECT A.CustomerID ,
                'Details' TableName  ,
                UploadID ,
                B.CustomerName ,
                A.AssetClass ,
                NPADate ,
                SecurityValue ,
                AdditionalProvision ,
                A.MOCType ,
                A.MOCReason ,
                A.MOCDate ,
                MOCBy ,
                MOCSource 
           FROM CustomerLevelMOC_Mod A
                  JOIN CustomerBasicDetail B   ON A.CustomerID = B.CustomerID
          WHERE  UploadId = v_ExcelUploadId
                   AND A.EffectiveFromTimeKey <= v_Timekey
                   AND A.EffectiveToTimeKey >= v_Timekey
                   AND B.EffectiveFromTimeKey <= v_Timekey
                   AND B.EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

