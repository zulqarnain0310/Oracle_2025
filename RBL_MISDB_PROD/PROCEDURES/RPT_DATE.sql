  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."RPT_DATE" 
(
  v_Year IN NUMBER,
  v_Month IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT UTILS.CONVERT_TO_VARCHAR2(DATE_,20,p_style=>103) DateLabel  ,
             TimeKey DateValue  
        FROM SysDayMatrix DDM
       WHERE  DDM.Date_ <= SYSDATE
                AND utils.year_(DDM.Date_) = v_Year
                AND utils.datename('MM', DDM.Date_) = v_Month
        ORDER BY DDM.Date_ DESC ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

/

