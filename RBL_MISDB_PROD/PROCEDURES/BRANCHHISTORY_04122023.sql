  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."BRANCHHISTORY_04122023" 
(
  v_Code IN NUMBER
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.BranchAlt_Key ,
             'BranchHistory' TableName  ,
             A.BranchCode ,
             A.BranchName ,
             A.Add_1 Address1  ,
             A.Add_2 Address2  ,
             A.Add_3 Address3  ,
             B.DistrictAlt_Key ,
             B.DistrictName ,
             C.StateAlt_Key ,
             C.StateName ,
             A.PinCode ,
             D.CountryAlt_Key ,
             D.CountryName ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModified,20,p_style=>103) DateModified  
        FROM DimBranch A
               JOIN DimGeography B   ON A.BranchDistrictAlt_Key = B.DistrictAlt_Key
               JOIN DimState C   ON A.BranchStateAlt_Key = C.StateAlt_Key
               JOIN DimCountry D   ON A.CountryAlt_Key = D.CountryAlt_Key
       WHERE  A.BranchCode = v_Code ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

