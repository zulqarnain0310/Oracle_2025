  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."CALYPSOCUSTOMERLEVELSEARCHDETAILS_04122023" 
---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

(
  v_UCICID IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT T.UCIfID UCICID  ,
             T.IssuerName CustomerName  ,
             D.AssetClass ,
             D.NPADate ,
             D.SecurityValue ,
             D.AdditionalProvision ,
             C.ParameterName FraudAccountFlag  ,
             D.FraudDate ,
             'CalypsoCustomerLevelMOC' TableName  
        FROM CalypsoCustomerLevelMOC D
               JOIN InvestmentIssuerDetail T   ON T.IssuerID = D.Customerid
               AND T.EffectiveFromTimeKey <= v_Timekey
               AND T.EffectiveToTimeKey >= v_Timekey
               LEFT JOIN ( SELECT parametername ,
                                  parameteralt_key 
                           FROM DimParameter 
                            WHERE  dimparametername = 'dimyesno'
                                     AND EffectiveFromTimeKey <= v_Timekey
                                     AND EffectiveToTimeKey >= v_Timekey ) C   ON D.FraudAccountFlagAlt_Key = C.ParameterAlt_Key
       WHERE  T.EffectiveFromTimeKey <= v_Timekey
                AND T.EffectiveToTimeKey >= v_Timekey
                AND T.UCIFID = v_UCICID
      UNION 
      SELECT A.UCIC_ID UCICID  ,
             A.CustomerName CustomerName  ,
             D.AssetClass ,
             D.NPADate ,
             D.SecurityValue ,
             D.AdditionalProvision ,
             C.ParameterName FraudAccountFlag  ,
             D.FraudDate ,
             'CalypsoCustomerLevelMOC' TableName  
        FROM CalypsoCustomerLevelMOC D
               JOIN CurDat_RBL_MISDB_PROD.DerivativeDetail A   ON D.Customerid = A.CustomerId
               AND A.EffectiveFromTimeKey <= v_Timekey
               AND A.EffectiveToTimeKey >= v_Timekey
               LEFT JOIN ( SELECT parametername ,
                                  parameteralt_key 
                           FROM DimParameter 
                            WHERE  dimparametername = 'dimyesno'
                                     AND EffectiveFromTimeKey <= v_Timekey
                                     AND EffectiveToTimeKey >= v_Timekey ) C   ON D.FraudAccountFlagAlt_Key = C.ParameterAlt_Key
       WHERE  A.EffectiveFromTimeKey <= v_Timekey
                AND A.EffectiveToTimeKey >= v_Timekey
                AND A.UCIC_ID = v_UCICID ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

