  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."TRANSACTIONSUBTYPEHISTORY" 
(
  v_Code IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.Transaction_Sub_TypeAlt_Key ,
             B.SourceName Source_System_Name  ,
             B.SourceAlt_Key ,
             A.Transaction_Sub_Type_Code ,
             A.Transaction_Sub_Type_Description ,
             A.CreatedBy ,
