  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."CONSTITUTIONHISTORY" 
(
  v_SrcSysConstitutionCode IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT A.ConstitutionMappingAlt_key ,
             B.SourceName ,
             A.SrcSysConstitutionCode BankCode  ,
             A.SrcSysConstitutionName BankDescription  ,
             A.ConstitutionAlt_Key Code  ,
             A.ConstitutionName ConDescription  ,
             A.CreatedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
             A.ApprovedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
             A.ModifiedBy ,
             UTILS.CONVERT_TO_VARCHAR2(A.DateModifie,20,p_style=>103) DateModified  
        FROM DimConstitutionMapping A
               JOIN DIMSOURCEDB B   ON A.SourceAlt_Key = B.SourceAlt_Key
       WHERE  SrcSysConstitutionCode = v_SrcSysConstitutionCode ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

