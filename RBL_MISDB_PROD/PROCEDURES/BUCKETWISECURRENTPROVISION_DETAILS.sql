  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."BUCKETWISECURRENTPROVISION_DETAILS" 
(
  v_SecuredType IN VARCHAR2 DEFAULT ' ' ,
  v_Segment IN VARCHAR2 DEFAULT ' ' ,
  v_AssetClassAlt_key IN NUMBER DEFAULT 0 ,
  v_BucketExceptCCAlt_key IN VARCHAR2 DEFAULT ' ' ,
  v_BucketCreditCardAlt_key IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_BorrowerName VARCHAR2(80) := ' ';
   v_CustomerEntityId NUMBER(10,0) := 0;
   v_AccountEntityId NUMBER(10,0) := 0;
   v_segmentcode VARCHAR2(10) := ' ';
   v_AssetClassName VARCHAR2(50) := ' ';
   v_segmentDescription VARCHAR2(10) := ' ';
   v_TimeKey NUMBER(10,0);
   v_SegmentName VARCHAR2(20);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT UPPER(AssetClassName) 

     INTO v_AssetClassName
     FROM DimAssetClass 
    WHERE  AssetClassAlt_Key = v_AssetClassAlt_key;
   --SET @Timekey =(Select Timekey from SysDataMatrix where CurrentStatus='C')  
   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  MOC_Initialised = 'Y'
             AND NVL(MOC_Frozen, 'N') = 'N';
   --Select distinct @SegmentName=AcBuSegmentDescription
   --	from DimAcBuSegment where AcBuRevisedSegmentCode=@Segment
   --DECLARE @SecuredType VARCHAR(15)='Secured'
   IF ( v_SecuredType = 'Secured' ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT ProvisionSecured 
           FROM DimProvision_Seg A
                  JOIN DimAssetClass acl   ON acl.EffectiveToTimeKey = 49999
                  AND acl.AssetClassShortNameEnum = a.ProvisionShortNameEnum
          WHERE  Segment = CASE 
                                WHEN v_SecuredType = 'Secured' THEN 'IRAC'
                 ELSE ' '
                    END
                   AND ACL.AssetClassName = v_AssetClassName ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      RETURN;

   END;
   END IF;
   DBMS_OUTPUT.PUT_LINE('@Segment');
   DBMS_OUTPUT.PUT_LINE(v_Segment);
   DBMS_OUTPUT.PUT_LINE('@@AssetClassName');
   DBMS_OUTPUT.PUT_LINE(v_AssetClassName);
   IF ( UPPER(v_Segment) = 'CREDIT CARD' ) THEN

   BEGIN
      DBMS_OUTPUT.PUT_LINE('SAC');
      OPEN  v_cursor FOR
         SELECT ProvisionUnSecured 
           FROM DimProvision_Seg a
                  JOIN DimAssetClass acl   ON acl.EffectiveToTimeKey = 49999
                  AND acl.AssetClassShortNameEnum = a.ProvisionShortNameEnum
          WHERE  Segment = UPPER(v_Segment)
                   AND A.ProvisionRule = (CASE 
                                               WHEN v_BucketCreditCardAlt_key = 'DPD 0-89 - bc2' THEN 'K/W/E/U'
                                               WHEN v_BucketCreditCardAlt_key = 'DPD 0-89 - Other' THEN 'OTHERS/BLANK'
                                               WHEN v_BucketCreditCardAlt_key = 'DPD 90' THEN 'K/W/E/U'
                                               WHEN v_BucketCreditCardAlt_key = 'DPD 90 - Other' THEN 'OTHERS/BLANK'   END)

                   --when @BucketCreditCardAlt_key='DPD 180' then 'K/W/E/U'

                   --when @BucketCreditCardAlt_key='DPD 180 – Other' then 'OTHERS/BLANK'
                   AND A.UpperDPD = (CASE 
                                          WHEN v_BucketCreditCardAlt_key IN ( 'DPD 0-89 - bc2','DPD 0-89 - Other' )
                                           THEN 89
                                          WHEN v_BucketCreditCardAlt_key IN ( 'DPD 90','DPD 90 - Other' )
                                           THEN 179   END)
                   AND ACL.AssetClassName = v_AssetClassName ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      RETURN;

   END;
   END IF;
   IF ( v_SecuredType = 'Unsecured' ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT ProvisionUnSecured 
           FROM DimProvision_Seg a
                  JOIN DimAssetClass acl   ON acl.EffectiveToTimeKey = 49999
                  AND acl.AssetClassShortNameEnum = a.ProvisionShortNameEnum
          WHERE  Segment = CASE 
                                WHEN v_SecuredType = 'Unsecured' THEN 'UNSECURED'
                 ELSE ' '
                    END
                   AND ACL.AssetClassName = v_AssetClassName
                   AND A.LowerDPD = (CASE 
                                          WHEN v_BucketExceptCCAlt_key = 'Q1' THEN 1
                                          WHEN v_BucketExceptCCAlt_key = 'Q2' THEN 2
                                          WHEN v_BucketExceptCCAlt_key = 'Q3' THEN 3   END) ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
      RETURN;

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

