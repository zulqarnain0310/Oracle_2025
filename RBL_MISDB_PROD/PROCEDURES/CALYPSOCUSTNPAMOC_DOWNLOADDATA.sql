  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."CALYPSOCUSTNPAMOC_DOWNLOADDATA" 
(
  iv_Timekey IN NUMBER,
  v_UserLoginId IN VARCHAR2,
  v_ExcelUploadId IN NUMBER,
  v_UploadType IN VARCHAR2,
  v_CURSOR OUT SYS_REFCURSOR
)
AS
   v_Timekey NUMBER(10,0) := iv_Timekey;
   v_COUNT INT;
   
--,@Page SMALLINT =1     
--   ,@perPage INT = 30000   
----DECLARE v_TIMEKEY INT=49999
----	,@UserLoginId VARCHAR(100)='FNASUPERADMIN'
