----	,v_UploadType VARCHAR(50)='Interest reversal'


BEGIN

   --SET v_TIMEKEY =(Select TimeKey from SysDataMatrix where CurrentStatus='C') 
   --SET v_TIMEKEY =(Select LastMonthDateKey from SysDayMatrix where Timekey=v_TIMEKEY) 
   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  MOC_Initialised = 'Y'
             AND NVL(MOC_Frozen, 'N') = 'N';
   DBMS_OUTPUT.PUT_LINE(v_Timekey);--DECLARE @PageFrom INT, @PageTo INT   
   --SET @PageFrom = (@perPage*@Page)-(@perPage) +1  
   --SET @PageTo = @perPage*@Page  
   --SELECT * FROM(
   IF v_UploadType = 'Calypso Customer MOC Upload'  
    THEN
       SELECT COUNT(1) INTO v_COUNT FROM CalypsoCustomerLevelMOC_Mod A INNER JOIN InvestmentIssuerDetail B ON A.UCIFID = B.UCIFID WHERE UploadId = v_ExcelUploadId AND A.EffectiveFromTimeKey <= v_TIMEKEY AND A.EffectiveToTimeKey >= v_TIMEKEY AND B.EffectiveFromTimeKey <= v_TIMEKEY AND B.EffectiveToTimeKey >= v_TIMEKEY ; 
           IF v_COUNT>0 
           THEN
            OPEN v_CURSOR FOR
                    SELECT DISTINCT A.UCIFID AS UCICID , 'Details' AS TableName , UploadID , B.IssuerName AS CustomerName , A.AssetClass , CAST( NPADate AS VARCHAR (25) )
                   AS NPADate , AdditionalProvision , A.MOCType , A.MOCReason , CAST( A.MOCDate AS VARCHAR(25))
                   AS MOCDate , MOCSource 
           FROM CalypsoCustomerLevelMOC_Mod A INNER JOIN InvestmentIssuerDetail B ON A.UCIFID = B.UCIFID WHERE UploadId = v_ExcelUploadId AND A.EffectiveFromTimeKey <= v_TIMEKEY AND A.EffectiveToTimeKey >= v_TIMEKEY AND B.EffectiveFromTimeKey <= v_TIMEKEY AND B.EffectiveToTimeKey >= v_TIMEKEY  ;
            END IF;
    END IF;
EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

