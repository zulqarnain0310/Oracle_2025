  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."OTS_MASTERDOWNLOAD" 
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
