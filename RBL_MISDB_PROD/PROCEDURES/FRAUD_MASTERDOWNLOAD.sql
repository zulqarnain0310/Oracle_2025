  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."FRAUD_MASTERDOWNLOAD" 
AS
   v_TimeKey NUMBER(10,0);
   --select 
   --			 MOCTypeName as FraudSource
   --			 ,'FraudSource' as TableName
   --			 from dimmoctype
   --			 where EffectiveFromTimeKey<=@Timekey And EffectiveToTimeKey>=@Timekey
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT CASE 
                  WHEN ParameterName = 'NO' THEN 'N'
             ELSE 'Y'
                END RFAReportedByBank  ,
             'RFA_Reported_By_Bank' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'DimYesNo'
                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT BankName NameofOtherBankReportingRFA  ,
             'Name_of_Other_Banks_Reporting_RFA' Tablename  
        FROM DimBankRP 
       WHERE  EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             'Provision_Proference' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'DimProvisionPreference'
                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

