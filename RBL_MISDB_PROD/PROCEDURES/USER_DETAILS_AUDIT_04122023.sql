        FROM DimUserInfo a
               JOIN UserLoginHistory c   ON a.UserLoginID = c.UserID
               JOIN DimUserDeptGroup D   ON a.DepartmentId = D.DeptGroupId
               JOIN SysUserActivityLog_Attendence b   ON b.LogCreatedBy = a.UserLoginID
               JOIN MetaScreenFieldDetail MD   ON B.MenuID = MD.MenuId
       WHERE  a.EffectiveToTimeKey = 49999
                AND a.Activate = 'Y'
                AND UTILS.CONVERT_TO_VARCHAR2(LoginTime,200) >= '2022-03-01'
                AND LogoutTime IS NOT NULL
        ORDER BY UserLoginID,
                 LoginTime ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

