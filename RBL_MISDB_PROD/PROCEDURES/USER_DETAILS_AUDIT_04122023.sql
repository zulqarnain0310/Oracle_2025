BEGIN

   OPEN  v_cursor FOR
      SELECT UserLoginID ,
             UserName ,
             Activate ,
             Email_ID ,
             MobileNo ,
             D.DeptGroupName ,
             Designation ,
             IP_Address ,
             LoginTime ,
             LogoutTime ,
