AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT C.TaggingAlt_Key ,
             D.ParameterName TaggingLevel  ,
             C.AccountID ,
             C.CustomerID ,
             A.CollateralID ,
             CollateralValueatSanctioninRs ,
             CollateralValueasonNPAdateinRs ,
             CollateralValueatthetimeoflastreviewinRs ,
             ValuationSourceAlt_Key ,
             B.SourceName ,
             UTILS.CONVERT_TO_VARCHAR2(ValuationDate,20,p_style=>103) ValuationDate  ,
             CurrentValue LatestCollateralValueinRs  ,
             ExpiryBusinessRule ,
             Periodinmonth ,
             UTILS.CONVERT_TO_VARCHAR2(ValuationExpiryDate,20,p_style=>103) ValueExpirationDate  
        FROM CurDat_RBL_MISDB_PROD.AdvSecurityValueDetail A
               JOIN CurDat_RBL_MISDB_PROD.AdvSecurityDetail E   ON A.SecurityEntityID = E.SecurityEntityID
               JOIN DIMSOURCEDB b   ON a.ValuationSourceAlt_Key = b.SourceAlt_Key
               JOIN CollateralMgmt c   ON a.CollateralID = c.CollateralID
               JOIN ( SELECT ParameterAlt_Key ,
                             ParameterName ,
                             'TaggingLevel' Tablename  
                      FROM DimParameter 
                       WHERE  DimParameterName = 'DimRatingType'
                                AND ParameterName NOT IN ( 'Guarantor' )
                     ) D   ON C.TaggingAlt_Key = D.ParameterAlt_Key ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

