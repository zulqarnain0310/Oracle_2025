AS
   --Declare @AccountID varchar(30)='1711212010245923'
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT C.CustomerName CustomerName  ,
             C.UCIF_ID UCIF_ID  ,
             C.CustomerId CustomerID  ,
             B.CustomerACID AccountID  ,
             'PUICustNameUCIC' TableName  
        FROM CustomerBasicDetail C
               JOIN AdvAcBasicDetail B   ON B.customerentityid = C.customerentityid
       WHERE  C.EffectiveFromTimeKey <= v_TimeKey
                AND C.EffectiveToTimeKey >= v_TimeKey
                AND B.EffectiveFromTimeKey <= v_TimeKey
                AND B.EffectiveToTimeKey >= v_TimeKey
                AND CustomerACID = v_AccountID ;
      DBMS_SQL.RETURN_RESULT(v_cursor);--select * from CustomerBasicdetail where CustomerID='22120366'
   --select * from advacbasicdetail where customerentityid='605'
