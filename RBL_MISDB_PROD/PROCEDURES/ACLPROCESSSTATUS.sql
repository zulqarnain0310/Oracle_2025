set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."ACLPROCESSSTATUS" 
AS
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE EXISTS ( SELECT 1 
                      FROM PRO_RBL_MISDB_PROD.AclRunningProcessStatus 
                       WHERE  RunningProcessName = 'InsertDataIntoHistTable'
                                AND Completed = 'N' );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'N' Status  ,
                'ACL Failed' Desc_  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   ELSE
      OPEN  v_cursor FOR
         SELECT 'Y' Status  ,
                'ACL Successfull' Desc_  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

