  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."GOLDHISTORY_PROD_04122023" 
(
  v_KaretMasterAlt_Key IN NUMBER
)
AS
   v_ProcessDate DATE;
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT DataEffectiveFromDate 

     INTO v_ProcessDate
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   --select @ProcessDate

   BEGIN
      OPEN  v_cursor FOR
         SELECT A.KaretMasterAlt_Key ,
                A.KaretMasterValueName ,
                UTILS.CONVERT_TO_VARCHAR2(A.KaretMasterValueDt,20,p_style=>103) KaretMasterValueDt  ,
                A.KaretMasterValueAmt ,
                A.SrcSysKaretValueCode ,
                A.SrcSysKaretValueName ,
                A.CreatedBy ,
                UTILS.CONVERT_TO_VARCHAR2(A.DateCreated,20,p_style=>103) DateCreated  ,
                A.ApprovedBy ,
                UTILS.CONVERT_TO_VARCHAR2(A.DateApproved,20,p_style=>103) DateApproved  ,
                A.ModifiedBy ,
                UTILS.CONVERT_TO_VARCHAR2(A.DateModifie,20,p_style=>103) DateModified  ,
                UTILS.CONVERT_TO_VARCHAR2(S1.Date_,20,p_style=>103) EffectiveFromDate  ,
                UTILS.CONVERT_TO_VARCHAR2(S2.Date_,20,p_style=>103) EffectiveToDate  
           FROM DimKaretMaster A
                  JOIN SysDayMatrix S1   ON S1.TimeKey = A.EffectiveFromTimeKey
                  JOIN SysDayMatrix S2   ON S2.TimeKey = A.EffectiveToTimeKey
          WHERE  A.KaretMasterAlt_Key = v_KaretMasterAlt_Key
                   AND A.KaretMasterValueDt BETWEEN v_ProcessDate AND v_ProcessDate - 15 ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

