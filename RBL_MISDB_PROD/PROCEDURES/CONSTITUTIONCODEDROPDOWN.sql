  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."CONSTITUTIONCODEDROPDOWN" 
(
  v_ConstitutionDescription IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  currentstatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT ConstitutionAlt_Key Code  ,
                ConstitutionName ConstitutionDescription  ,
                'CrismacCode' TableName  
           FROM DimConstitution 
          WHERE  EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey
                   AND ConstitutionName = v_ConstitutionDescription ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

