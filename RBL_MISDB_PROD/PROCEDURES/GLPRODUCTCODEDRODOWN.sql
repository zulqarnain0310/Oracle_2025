  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."GLPRODUCTCODEDRODOWN" 
(
  --Declare 
  v_ProductName IN VARCHAR2
)
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  currentstatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT A.ProductName ,
                A.ProductCode ,
                B.SourceAlt_Key ,
                B.SourceName ,
                'GLProductCodeSourceList' TableName  
           FROM DimProduct A
                  JOIN DIMSOURCEDB B   ON A.SourceAlt_Key = B.SourceAlt_Key
                  AND B.EffectiveFromTimeKey <= v_Timekey
                  AND B.EffectiveToTimeKey >= v_Timekey
          WHERE  A.EffectiveFromTimeKey <= v_Timekey
                   AND A.EffectiveToTimeKey >= v_Timekey
                   AND ProductName = v_ProductName ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

