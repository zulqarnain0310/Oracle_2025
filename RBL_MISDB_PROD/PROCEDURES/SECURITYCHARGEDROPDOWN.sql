set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SECURITYCHARGEDROPDOWN" 
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT TimeKey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT SecurityChargeTypeName ,
                SecurityChargeTypeAlt_key Code  ,
                'CrisMacDesc' TableName  
           FROM DimSecurityChargeType 
          WHERE  EffectiveFromTimeKey <= v_Timekey
                   AND EffectiveToTimeKey >= v_Timekey
                   AND SecurityChargeTypeGroup = 'COLLATERAL' ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

