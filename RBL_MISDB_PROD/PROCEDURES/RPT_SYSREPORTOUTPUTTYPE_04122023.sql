(
  v_ReportType IN VARCHAR2
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   ----Declare 
   ----@ReportType AS Varchar(10)=6
   IF ( v_ReportType IN ( 1,5 )
    ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Summary' CustFacilityLabel  ,
                '3' CustFacilityValue  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;
   IF ( v_ReportType IN ( 2 )
    ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Customer Wise' CustFacilityLabel  ,
                '1' CustFacilityValue  
           FROM DUAL 
         UNION ALL 
         SELECT 'Facility Wise' CustFacilityLabel  ,
                '2' CustFacilityValue  
           FROM DUAL 
         UNION ALL 
         SELECT 'Summary' CustFacilityLabel  ,
                '3' CustFacilityValue  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;
   IF ( v_ReportType IN ( 6 )
    ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Customer Wise' CustFacilityLabel  ,
                '1' CustFacilityValue  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;
   IF ( v_ReportType = 3 ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Summary' CustFacilityLabel  ,
                '3' CustFacilityValue  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;
   IF ( v_ReportType = 9 ) THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT 'Summary' CustFacilityLabel  ,
                '9' CustFacilityValue  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

