)
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT A.Entity_Key ,
                A.DirectorName ,
                A.PAN ,
                A.DIN ,
                A.DirectorTypeAlt_Key ,
                B.DimParameterName DirectoryType  ,
                AuthorisationStatus.A.EffectiveFromTimeKey ,
                EffectiveToTimeKey_A.A.CreatedBy ,
                A.DateCreated ,
                A.ModifiedBy ,
                A.DateModified ,
                A.ApprovedBy ,
                A.DateApproved 
           FROM WillfulDirectorDetail A
                  JOIN ( SELECT ParameterAlt_Key ,
                                ParameterName ,
                                'DirectorType' Tablename  
                         FROM DimParameter 
                          WHERE  DimParameterName = 'DirectorType'
                                   AND EffectiveFromTimeKey <= v_TimeKey
                                   AND EffectiveToTimeKey >= v_TimeKey ) B   ON A.DirectorTypeAlt_Key = B.ParameterAlt_Key
                  AND v_DirectoreName = v_DirectoreName
                  OR v_Pan = v_Pan
                  OR v_Din = v_Din
                  OR v_DirectorType = v_DirectorType ;
         DBMS_SQL.RETURN_RESULT(v_cursor);--Inner Join DimBank B
      --ON B.BankAlt_Key=A.ReportedByAlt_Key
      --AND B.EffectiveFromTimeKey<=@TimeKey And B.EffectiveToTimeKey>=@TimeKey
      --where A.EffectiveFromTimeKey<=@TimeKey And A.EffectiveToTimeKey>=@TimeKey
      --And  @ReportingBank = @ReportingBank
      --OR @CustomerID	= @CustomerID	
      --OR @PartyName	= @PartyName			  
      --OR @PAN			= @PAN		

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

