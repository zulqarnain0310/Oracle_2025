   v_TimeKey NUMBER(10,0);
   --END
   v_cursor SYS_REFCURSOR;

BEGIN

   --SET @Timekey =(Select Timekey from SysDataMatrix where CurrentStatus='C')
   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  MOC_Initialised = 'Y'
             AND NVL(MOC_Frozen, 'N') = 'N';
   --------- ADD LOGIC TO CHECK THE ACL PROCESS IS RUNNING OR NOT BY SATWAJI AS ON 19/08/2023 ----------------
   --IF (@OperationFlag IN(1,2,3,16,17,20))
   --BEGIN
   DBMS_OUTPUT.PUT_LINE('ACL Process Status Check');
   ACLProcessStatusCheck() ;
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             'AcceleratedProvisionDuration' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'DimAccProvDuration'
                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey
        ORDER BY ParameterAlt_Key ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   --Select ParameterAlt_Key
   --,ParameterName
   --,'ProvisionPointerMaster' as Tablename 
   --from DimParameter where DimParameterName='DimRatingType'
   --and ParameterName not in ('Guarantor')
   --and EffectiveFromTimeKey<=@TimeKey And EffectiveToTimeKey>=@TimeKey
   --Order By ParameterAlt_Key
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             'SecureUnsecureMaster' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'DimExposureType'
                AND ParameterName NOT IN ( 'Derived' )

                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey
        ORDER BY ParameterAlt_Key ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT DISTINCT AcBuRevisedSegmentCode EWS_Segment_Key  ,
                      --AcBuSegmentAlt_Key as EWS_Segment_Key
                      --,AcBuSegmentDescription EWS_SegmentShortName
                      AcBuRevisedSegmentCode EWS_SegmentShortName  ,
                      'SegmentMaster' TableName  
        FROM DimAcBuSegment A
       WHERE  A.EffectiveFromTimeKey <= v_TimeKey
                AND A.EffectiveToTimeKey >= v_TimeKey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT AssetClassAlt_Key ,
             AssetClassName ,
             'DimAssetClass' TableName  
        FROM DimAssetClass A
       WHERE  A.EffectiveFromTimeKey <= v_TimeKey
                AND A.EffectiveToTimeKey >= v_TimeKey
                AND AssetClassAlt_Key NOT IN ( 1,5,6 )
    ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             ParameterShortName ,
             'DimBucketExceptCC' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'DimBucketExceptCC'
                AND ParameterAlt_Key NOT IN ( 4,5 )

                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey
        ORDER BY ParameterAlt_Key ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT ParameterAlt_Key ,
             ParameterName ,
             ParameterShortName ,
             'DimBucketCreditCard' Tablename  
        FROM DimParameter 
       WHERE  DimParameterName = 'BucketCreditCard'
                AND ParameterAlt_Key NOT IN ( 4,6 )

                AND EffectiveFromTimeKey <= v_TimeKey
                AND EffectiveToTimeKey >= v_TimeKey
        ORDER BY ParameterAlt_Key ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT * ,
             'BucketLevelApproach' TableName  
        FROM MetaScreenFieldDetail 
       WHERE  ScreenName = 'BucketLevelApproach' ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

