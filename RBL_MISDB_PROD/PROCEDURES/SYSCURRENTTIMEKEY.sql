BEGIN

   /*TODO:SQLDEV*/ SET DATEFORMAT DMY /*END:SQLDEV*/
   DECLARE
      v_TIMEKEY NUMBER(10,0);

   BEGIN
      SELECT A.TimeKey 

        INTO v_TIMEKEY
        FROM SysDayMatrix a
               JOIN SysDataMatrix b   ON A.Date = b.MonthFirstDate
       WHERE  b.CurrentStatus = 'C';
      OPEN  v_cursor FOR
         SELECT UTILS.CONVERT_TO_CHAR(DataEffectiveFromDate,30,p_style=>103) MonthFirstDate  ,
                UTILS.CONVERT_TO_CHAR(DataEffectiveToDate,30,p_style=>103) MonthLastDate  ,
                UTILS.CONVERT_TO_CHAR('01/01/1950',30,p_style=>103) StartDate  ,
                --,@TIMEKEY AS TimeKey       
                MonthName ,
                CASE 
                     WHEN utils.month_(DataEffectiveToDate) <= 9 THEN '0' || UTILS.CONVERT_TO_VARCHAR2(utils.month_(DataEffectiveToDate),30)
                ELSE UTILS.CONVERT_TO_VARCHAR2(utils.month_(DataEffectiveToDate),30)
                   END || UTILS.CONVERT_TO_VARCHAR2(utils.year_(DataEffectiveToDate),30) MonthYear  ,
                Year ,
                Prev_Month_Key PrvTimeKey  ,
                CurrentStatus ,
                v_TIMEKEY EffectiveFromTimeKey  ,
                '49999' EffectiveToTimeKey  ,
                DataEffectiveToDate ,
                UTILS.CONVERT_TO_VARCHAR2(SYSDATE,11,p_style=>103) CurrentDate  ,
                B.TimeKey TimeKey  ,
                A.MonthName || ' (' || UTILS.CONVERT_TO_VARCHAR2(A.MonthFirstDate,10,p_style=>103) || ' - ' || UTILS.CONVERT_TO_VARCHAR2(A.MonthLastDate,10,p_style=>103) || ')' FreezeDate  
           FROM SysDataMatrix A
                  JOIN SysDayMatrix B   ON a.MonthLastDate = b.Date_
          WHERE  CurrentStatus = 'C' ;
         DBMS_SQL.RETURN_RESULT(v_cursor);--	BEGIN    
      --    SELECT TimeKey AS EffectiveFromTimeKey,  
      --     '9999' AS EffectiveToTimeKey  
      -- FROM SysDayMatrix   
      -- WHERE Date = (Select MonthFirstDate from SysDataMatrix  
      -- WHERE CurrentStatus = 'C'  )  
      --END  

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

