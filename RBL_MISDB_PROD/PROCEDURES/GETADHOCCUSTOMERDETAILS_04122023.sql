  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."GETADHOCCUSTOMERDETAILS_04122023" 
(
  v_UCIF_ID IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_Timekey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT TimeKey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   IF utils.object_id('TEMPDB..tt_TMP_UCIC') IS NOT NULL THEN
    EXECUTE IMMEDIATE ' TRUNCATE TABLE tt_TMP_UCIC ';
   END IF;
   DELETE FROM tt_TMP_UCIC;
   UTILS.IDENTITY_RESET('tt_TMP_UCIC');

   INSERT INTO tt_TMP_UCIC ( 
   	SELECT DISTINCT UcifEntityID 
   	  FROM CustomerBasicDetail 
   	 WHERE  ( EffectiveFromTimeKey <= v_Timekey
              AND EffectiveToTimeKey >= v_Timekey )
              AND UCIF_ID = v_UCIF_ID );
   IF utils.object_id('TEMPDB..tt_TMP_1') IS NOT NULL THEN
    EXECUTE IMMEDIATE ' TRUNCATE TABLE tt_TMP_1 ';
   END IF;
   --DROP Table IF Exists #tmp_1
   DELETE FROM tt_TMP_1;
   UTILS.IDENTITY_RESET('tt_TMP_1');

   INSERT INTO tt_TMP_1 SELECT * 
        FROM ( SELECT A.UCIF_ID UCICID  ,
                      A.RefCustomerID CustomerId  ,
                      A.CustomerName ,
                      'AdhocCustDetails' TableName  ,
                      ROW_NUMBER() OVER ( ORDER BY A.RefCustomerID DESC  ) RowNumber  
               FROM PRO_RBL_MISDB_PROD.CustomerCal_Hist A
                      JOIN tt_TMP_UCIC B   ON A.UcifEntityID = B.UcifEntityID
                WHERE  ( A.EffectiveFromTimeKey <= v_Timekey
                         AND A.EffectiveToTimeKey >= v_Timekey ) ) R
       WHERE  RowNumber = 1;
   OPEN  v_cursor FOR
      SELECT * 
        FROM tt_TMP_1  ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

