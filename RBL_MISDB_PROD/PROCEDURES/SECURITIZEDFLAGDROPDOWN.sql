  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."SECURITIZEDFLAGDROPDOWN" 
AS
   v_Timekey NUMBER(10,0);
   v_CustomerACID VARCHAR2(50);
   v_cursor SYS_REFCURSOR;

BEGIN

   --Set @Timekey =(Select TimeKey from SysDataMatrix where CurrentStatus='C')
   SELECT UTILS.CONVERT_TO_NUMBER(B.timekey,10,0) 

     INTO v_Timekey
     FROM SysDataMatrix A
            JOIN SysDayMatrix B   ON A.TimeKey = B.TimeKey
    WHERE  A.CurrentStatus = 'C';

   BEGIN
      OPEN  v_cursor FOR
         SELECT PoolID ,
                PoolName ,
                SecuritisationType ,
                'PoolList' TableName  
           FROM SecuritizedFinalACSummary A
          WHERE  A.EffectiveFromTimeKey <= v_Timekey
                   AND A.EffectiveToTimeKey >= v_Timekey ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

