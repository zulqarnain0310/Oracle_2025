(
  v_InvID IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_TimeKey NUMBER(10,0);
   v_temp NUMBER(1, 0) := 0;
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   BEGIN
      SELECT 1 INTO v_temp
        FROM DUAL
       WHERE NOT EXISTS ( SELECT 1 
                          FROM CurDat_RBL_MISDB_PROD.InvestmentBasicDetail A
                                 LEFT JOIN CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail B   ON A.RefIssuerID = B.IssuerID
                           WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                                    AND A.EffectiveToTimeKey >= v_TimeKey )
                                    AND A.InvID = v_InvID
                                    AND NVL(A.AuthorisationStatus, 'A') = 'A' );
   EXCEPTION
      WHEN OTHERS THEN
         NULL;
   END;

   IF v_temp = 1 THEN

   BEGIN
      DELETE FROM tt_temp1_9;
      UTILS.IDENTITY_RESET('tt_temp1_9');

      INSERT INTO tt_temp1_9 ( 
      	SELECT A.* ,
              IssuerID ,
              IssuerName ,
              InstrumentTypeName ,
              IndustryName 
      	  FROM CurDat_RBL_MISDB_PROD.InvestmentBasicDetail A
                LEFT JOIN CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail B   ON A.RefIssuerID = B.IssuerID
                LEFT JOIN DimInstrumentType C   ON A.InstrTypeAlt_Key = C.InstrumentTypeAlt_Key
                LEFT JOIN DimIndustry D   ON A.Industry_AltKey = D.IndustryAlt_Key
      	 WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                 AND A.EffectiveToTimeKey >= v_TimeKey )
                 AND A.InvId = v_InvID
                 AND NVL(A.AuthorisationStatus, 'A') = 'A' );
      OPEN  v_cursor FOR
         SELECT ( SELECT ROW_NUMBER() OVER ( ORDER BY InvEntityID  ) RowNumber  
                    FROM DUAL  ) ,
                COUNT(*) OVER ( ) TotalCount  ,
                'InvestmentCodeMaster' TableName  ,
                * 
           FROM tt_temp1_9  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   ELSE

   BEGIN
      DELETE FROM tt_temp_171;
      UTILS.IDENTITY_RESET('tt_temp_171');

      INSERT INTO tt_temp_171 ( 
      	SELECT A.* ,
              IssuerID ,
              IssuerName ,
              InstrumentTypeName ,
              IndustryName 
      	  FROM CurDat_RBL_MISDB_PROD.InvestmentBasicDetail A
                LEFT JOIN CurDat_RBL_MISDB_PROD.InvestmentIssuerDetail B   ON A.RefIssuerID = B.IssuerID
                LEFT JOIN DimInstrumentType C   ON A.InstrTypeAlt_Key = C.InstrumentTypeAlt_Key
                LEFT JOIN DimIndustry D   ON A.Industry_AltKey = D.IndustryAlt_Key
      	 WHERE  ( A.EffectiveFromTimeKey <= v_TimeKey
                 AND A.EffectiveToTimeKey >= v_TimeKey )
                 AND A.InvId = v_InvID
                 AND NVL(A.AuthorisationStatus, 'A') = 'A' );
      OPEN  v_cursor FOR
         SELECT ( SELECT ROW_NUMBER() OVER ( ORDER BY InvEntityID  ) RowNumber  
                    FROM DUAL  ) ,
                COUNT(*) OVER ( ) TotalCount  ,
                'InvestmentCodeMaster' TableName  ,
                * 
           FROM tt_temp_171  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;
   END IF;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

