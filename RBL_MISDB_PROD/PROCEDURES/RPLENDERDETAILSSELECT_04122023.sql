  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_MISDB_PROD"."RPLENDERDETAILSSELECT_04122023" 
--RP_Lender_Details

(
  v_CustomerID IN VARCHAR2 DEFAULT ' ' 
)
AS
   v_TimeKey NUMBER(10,0);
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT Timekey 

     INTO v_Timekey
     FROM SysDataMatrix 
    WHERE  CurrentStatus = 'C';
   OPEN  v_cursor FOR
      SELECT A.CustomerID ,
             B.BankName ReportingLenderName  ,
             (CASE 
                   WHEN UTILS.CONVERT_TO_VARCHAR2(A.InDefaultDate,200) = ' ' THEN NULL
             ELSE UTILS.CONVERT_TO_VARCHAR2(InDefaultDate,20,p_style=>103)
                END) InDefaultDate  ,
             (CASE 
                   WHEN UTILS.CONVERT_TO_VARCHAR2(A.OutOfDefaultDate,200) = ' ' THEN NULL
             ELSE UTILS.CONVERT_TO_VARCHAR2(OutOfDefaultDate,20,p_style=>103)
                END) OutOfDefaultDate  ,
             A.DefaultStatus ,
             'LenderGridData' TableName  
        FROM RP_Lender_Details A
               JOIN DimBankRP B   ON A.ReportingLenderAlt_Key = B.BankRPAlt_Key
               AND B.EffectiveFromTimeKey <= v_Timekey
               AND B.EffectiveToTimeKey >= v_TimeKey
       WHERE  A.CustomerID = v_CustomerID
                AND A.EffectiveFromTimeKey <= v_Timekey
                AND A.EffectiveToTimeKey >= v_TimeKey ;
      DBMS_SQL.RETURN_RESULT(v_cursor);--exec RPLenderDetailsSelect @CustomerID=1

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

