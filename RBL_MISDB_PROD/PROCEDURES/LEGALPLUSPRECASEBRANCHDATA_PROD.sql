 -- Description:	<Description,,>
 -- =============================================

(
  v_UserLoginID IN VARCHAR2 DEFAULT ' ' ,
  v_TimeKey IN NUMBER DEFAULT 9999 
)
AS
   v_LocatationCode VARCHAR2(10);
   v_Location CHAR(2) := 'HO';
   v_cursor SYS_REFCURSOR;

BEGIN

   SELECT NVL(UserLocation, ' ') ,
          NVL(UserLocationCode, ' ') 

     INTO v_Location,
          v_LocatationCode
     FROM DimUserInfo 
    WHERE  UserLoginID = v_UserLoginID;
   DBMS_OUTPUT.PUT_LINE(v_Location);
   DBMS_OUTPUT.PUT_LINE(v_LocatationCode);
   IF utils.object_id('Tempdb..tt_TempBrData_4') IS NOT NULL THEN
    EXECUTE IMMEDIATE ' TRUNCATE TABLE tt_TempBrData_4 ';
   END IF;
   DELETE FROM tt_TempBrData_4;
   INSERT INTO tt_TempBrData_4
     ( SELECT BranchCode ,
              BranchName 
       FROM DimBranch A
        WHERE  ( EffectiveFromTimeKey <= v_TimeKey
                 AND EffectiveToTimeKey >= v_TimeKey )
                 AND v_LocatationCode = CASE 
                                             WHEN v_Location = 'HO' THEN v_LocatationCode
                                             WHEN v_Location = 'ZO' THEN UTILS.CONVERT_TO_VARCHAR2(A.BranchZoneAlt_Key,10)
                                             WHEN v_Location = 'RO' THEN UTILS.CONVERT_TO_VARCHAR2(A.BranchRegionAlt_Key,10)
                                             WHEN v_Location = 'BO' THEN UTILS.CONVERT_TO_VARCHAR2(A.BranchCode,10)   END );
   OPEN  v_cursor FOR
      SELECT * 
        FROM tt_TempBrData_4  ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

