DESCRIPTION: UpdationProvisionComputationUnSecured          
--EXEC  [dbo].[UpdationProvisionComputationUnSecured] @TimeKey =1                                     

===============================================*/
(
  v_TimeKey IN NUMBER
)
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   BEGIN

      BEGIN
         --------------------------------------------<Provision Compuatation on UnSecured Portion>---------------------------------------                        
         MERGE INTO A 
         USING (SELECT A.ROWID row_id, 0, 0
         FROM A ,RBL_MISDB_PROD.ACCOUNTCAL A
                JOIN DimProvision_Seg DP   ON A.ProvisionAlt_Key = DP.ProvisionAlt_Key ) src
         ON ( A.ROWID = src.row_id )
         WHEN MATCHED THEN UPDATE SET UnSecuredAmt = 0,
                                      ProvUnsecured = 0;
         MERGE INTO A 
         USING (SELECT A.ROWID row_id, CASE 
         WHEN (((NVL(A.NetBalance, 0) - NVL(A.SecuredAmt, 0)))) > 0 THEN (((NVL(A.NetBalance, 0) - NVL(A.SecuredAmt, 0))))
         ELSE 0
            END AS pos_2, ((NVL(A.NetBalance, 0) - (NVL(A.SecuredAmt, 0))) * (NVL(B.ProvisionUnSecured, 0) / 100)) AS pos_3
         FROM A ,RBL_MISDB_PROD.ACCOUNTCAL A
                JOIN DimProvision_Seg B   ON A.ProvisionAlt_Key = B.ProvisionAlt_Key
                JOIN DimAssetClass C   ON C.AssetClassAlt_Key = A.FinalAssetClassAlt_Key
                AND ( c.EffectiveFromTimeKey <= v_TimeKey
                AND c.EffectiveToTimeKey >= v_TimeKey ) ) src
         ON ( A.ROWID = src.row_id )
         WHEN MATCHED THEN UPDATE SET A.UnSecuredAmt = pos_2,
                                      A.ProvUnsecured = pos_3;
         UPDATE ADVACCAL
            SET PROVUNSECURED = 0
          WHERE  PROVUNSECURED < 0;

      END;
   EXCEPTION
      WHEN OTHERS THEN

   BEGIN
      OPEN  v_cursor FOR
         SELECT SQLERRM ERROR_MESSAGE  
           FROM DUAL  ;
         DBMS_SQL.RETURN_RESULT(v_cursor);

   END;END;

EXCEPTION WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

