  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_STGDB"."AUTOMATEADVANCESTIMECHANGE" 
AS
   v_Date VARCHAR2(200);

BEGIN

   SELECT DISTINCT DateofData 

     INTO v_Date
     FROM CUSTOMER_ALL_SOURCE_SYSTEM ;
   UPDATE RBL_STGDB.AUTOMATE_ADVANCES
      SET Ext_flg = 'N'
    WHERE  Ext_flg = 'Y';
   UPDATE RBL_STGDB.AUTOMATE_ADVANCES
      SET Ext_flg = 'Y'
    WHERE  UTILS.CONVERT_TO_VARCHAR2(date_,200) = v_Date;
   UPDATE RBL_MISDB_PROD.Automate_Advances
      SET Ext_flg = 'N'
    WHERE  Ext_flg = 'Y';
   UPDATE RBL_MISDB_PROD.Automate_Advances
      SET Ext_flg = 'Y'
    WHERE  UTILS.CONVERT_TO_VARCHAR2(date_,200) = v_Date;
   UPDATE RBL_MISDB_PROD.SysDataMatrix
      SET Currentstatus = 'U'
    WHERE  Currentstatus = 'C';
   UPDATE RBL_MISDB_PROD.SysDataMatrix
      SET Currentstatus = 'C'
    WHERE  UTILS.CONVERT_TO_VARCHAR2(MonthLastdate,200) = v_Date;

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

