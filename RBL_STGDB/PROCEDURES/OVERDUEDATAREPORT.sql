set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "RBL_STGDB"."OVERDUEDATAREPORT" 
AS
   v_cursor SYS_REFCURSOR;

BEGIN

   OPEN  v_cursor FOR
      SELECT DISTINCT CustomerAcID ,
                      PrincipalOverdueAmt ,
                      PrincipalOverDueSinceDt 
        FROM RBL_STGDB.ACCOUNT_ALL_SOURCE_SYSTEM 
       WHERE  PrincipalOverdueAmt > 0
                AND PrincipalOverDueSinceDt IS NULL ;
      DBMS_SQL.RETURN_RESULT(v_cursor);
   OPEN  v_cursor FOR
      SELECT DISTINCT CustomerAcID ,
                      InterestOverdueAmt ,
                      InterestOverDueSinceDt 
        FROM RBL_STGDB.ACCOUNT_ALL_SOURCE_SYSTEM 
       WHERE  InterestOverdueAmt > 0
                AND InterestOverDueSinceDt IS NULL ;
      DBMS_SQL.RETURN_RESULT(v_cursor);

EXCEPTION WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
            WHEN OTHERS THEN utils.handleerror(SQLCODE,SQLERRM);
END;

